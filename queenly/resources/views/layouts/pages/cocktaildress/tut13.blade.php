<p><strong>We want to make the seam really flat.</strong> You can stamp it with a regular triangle ruler or a special cushion. The point of this procedure is to cool the seam down, dab it dry, and thereby make it flat and barely noticeable. It should be very flat and very even.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image076.jpg" alt=""></p>
