<p><strong>Now it's time to press open the princess seams on the interfacing. </strong></p>
<p><strong>Please note that I start pressing it right in the middle of the cup (the bust line) and not at the beginning or the end of the seam.</strong> And then I start carefully moving in both directions. It is a small detail but it really helps. Try to press it open in the traditional way first, i.e. by starting from the beginning of the seam, and then try to do it the way I suggest you should do it. You will see that my method is a lot faster and easier.</p>
