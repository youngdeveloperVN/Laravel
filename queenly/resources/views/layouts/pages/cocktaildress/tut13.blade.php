<h1>Tutorial 13. Sewing Down the Princess Seams of the Front.</h1>
<p><strong>Now we are going to sew down the bust princess seams of the dress.</strong> I join the interfacing pieces and the main pieces separately. Remember how we assembled the mock-up.</p>
<p><strong>Using a 1.2cm seam allowance, I sew down the first princess seam to join the interfacing pieces.</strong> I check the quality of the seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image002.jpg" alt=""></p>
<p><strong>I leave about half the initial width of the seam allowance and trim the ends at an angle.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image008.jpg" alt=""></p>
<p><strong>I do the same with the other bust princess seam. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image010.jpg" alt=""></strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image012.jpg" alt=""></p>
<p><strong>I join the non-duplicated main pieces by sewing down the bust princess seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image020.jpg" alt=""></p>
<p><strong>When you learn to sew neat and accurate bust princess seams, you will approach the level of proficiency required for sewing corset garments.</strong> Sewing these princess seams is the biggest challenge faced by seamstresses, and especially by beginners. They try to make things better by basting or pinning the raw edges together but this rarely helps. Actually, I would not recommend you ever baste anything in corset garments!</p>
<p><strong>You can only trim the seam allowance at the level of the cup here.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image024.jpg" alt=""></p>
<p><strong>Wide seam allowances will make it hard to press the garment.</strong> If you are a beginner though, then I recommend you first press those seam allowances open, then trim them, and then press them again. It is simply harder to press seam allowances after they have been trimmed.</p>
<p><strong>Let me leave the seam allowance of the other bust princess seam untrimmed</strong> so I can then demonstrate the peculiarities of pressing trimmed and untrimmed seam allowances.</p>
<p><strong>I sew down the other bust princess seam.</strong> Make sure to make a very even row of stitches. Only in this case will the bust area and the cups look neat. Be careful to align all notches in the vertical areas. What concerns the cups, you need to align the points of intersection of the bust line and the princess seam line (just like we did on the mock-up).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image030.jpg" alt=""></p>
<p><strong>I only trim the corner of the seam allowance at an angle and leave the rest of it as is.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image032.jpg" alt=""></p>
<p><strong>Let us start pressing. </strong></p>
<p><strong>I use a cup pressing mould (it is described in a separate tutorial dedicated to tools and equipment).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image034.jpg" alt=""></p>
<p><strong>I put the mould on a sleeve-pressing board and press the princess seam with the trimmed seam allowance.</strong> I simply spread the princess seam apart with my fingers and press it. Although it may seem easy, I know that certain difficulties might arise if you are doing it for the first time. The seam might not "listen" to your commands.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image040.jpg" alt=""></p>
<p><strong>You should pay particular attention to the top end of the seam allowance</strong> which you have trimmed at an angle. Make sure to spread it out and flatten it properly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image044.jpg" alt=""></p>
<p><strong>Next, you can press it in a circular motion to make the cup rounder.</strong> The fabric is soft and itï¿½s not like it will hold the shape but it will nevertheless settle better in the future. I wait for it to cool down.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image046.jpg" alt=""></p>
<p><strong>And then I press open the princess seam with the untrimmed seam allowance.</strong> It is much easier to press this seam allowance. It is much easier to spread it out at the ends, too. The pressing process takes less time but, on the other hand, I will still need to trim the seam allowance afterwards.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image050.jpg" alt=""></p>
<p><strong>I trim the seam allowance and press it again.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image056.jpg" alt=""></p>
<p><strong>Next, I turn out the garment, put the cup onto the mould, and press it from the other side.</strong> Just make sure the seam allowances stay open.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image060.jpg" alt=""></p>
<p><strong>Here is what the bust princess seam is supposed to look like after you press it:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image062.jpg" alt=""></p>
<p><strong>I press open the rest of the seams.</strong> If the seams "disobey," do not try to press them harder. Instead, you will simply achieve the desired result by pressing them from the face side as well. Right now your task is just to spread them open.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image064.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image066.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image068.jpg" alt=""></p>
<p><strong>I turn the garment around and finish pressing the seams.</strong> This time I pay more attention to the quality of the seam and try to eliminate any bumps or irregularities.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2013_files/image070.jpg" alt=""></p>
