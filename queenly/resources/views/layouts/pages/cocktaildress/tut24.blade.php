<h1>Tutorial 24. Covering the Side Seams by the Lining.</h1>
<p><strong>I have carried out a fitting, which was quite successful. </strong></p>
<p><strong>What flaws have I noticed?</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image002.jpg" alt=""></p>
<p>My intuition has betrayed me: I should not have altered the pattern in the hips. I should have strictly followed the pattern the dress was cut after. I have removed the minor adjustments in the hips and restored the initial shape of the pattern.</p>
<p>The pattern of our non-corset dress does not account for generous seams. As the result, the dress is somewhat tighter than it should be. I have ripped the side seams in the area from the under-bust line to the hip line, shifted them 0.5cm outwards, and restitched them. I have gradually brought it to 0cm at the bottom. The side seams had 2cm seam allowances so I had no problems applying those alterations.</p>
<p>I have determined the length of the shoulder strap and the place where it should be sewn.</p>
<p><strong>What did the fitting reveal about the back of the dress? </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image004.jpg" alt=""></p>
<p>Since it is a non-corset dress, there is a crease formed at the top of the back (in the middle). This occurs naturally. I really feel like installing bones into the side seams and at the back of the dress to somewhat spread it out. I know we had arranged that we were not sewing a corset dress but I'm sure you will have nothing against two small bones.</p>
<p>The dress holds its shape well at the sides and the small crease at the back is nothing out of the ordinary when there are no bones to support it.</p>
<p><strong>And, finally, the last comment I want to make:</strong></p>
<p>if you are sewing your dress with knitted fabric that stretches width-wise, then you can safely stick to the pattern without any alterations. But if you are sewing it with some non-stretch fabric, then you can use the regular 1.2cm seam allowance for the side seams (instead of 2cm) without changing their configuration.</p>
<p><strong>We are approaching the final stages of working on the dress.</strong></p>
<p><strong>The side seams need to be covered by the lining. </strong></p>
<p><strong>I turn the garment out.</strong> As a result of all the alterations that took place after the fitting, the seam allowance is 1.2cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image006.jpg" alt=""></p>
<p><strong>Letï¿½s cover the seam allowance of the side seam by the lining. </strong></p>
<p><strong>I pull the lining out from the opening at the top and wrap it over the edge of the dress. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image010.jpg" alt=""></p>
<p><strong>And then I start aligning all notches of the face and the lining.</strong> There is no need in piercing the garment right through when pinning the lining onto the face.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image012.jpg" alt=""></p>
<p><strong>I align the bottom edge of the lining with the turned hem of the main part of the dress.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image014.jpg" alt=""></strong></p>
<p><strong>And then I cover the other side seam by the lining just the same way.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image016.jpg" alt=""></p>
<p><strong>Next I sew down the side seams. </strong></p>
<p><strong>It is best to sew from the side which already has a side seam on it.</strong> You sew right over that seam or step 1mm toward the seam allowance. I also recommend you step 0.5cm down from the top edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image020.jpg" alt=""></p>
<p>You can serge the seam allowance of this seam but it won't be so bad if you don't. After you turn the garment out, the seam will be hidden inside and serged edges will only make it thicker. I remove the pins.</p>
<p><strong>I sew the other side seam from the bottom upwards. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image024.jpg" alt=""></p>
<p><strong>And now comes a very important moment: I need to turn out the corners at the top.</strong> Those are very thick areas.</p>
<p><strong>You can trim the seam allowance at an angle</strong> to leave as little fabric as possible. You should be able to turn the corners out properly afterwards.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image025.jpg" alt=""></p>
<p><strong>This is what the side seams look like from the inside after you turn the dress out.</strong> They're all hidden inside. And the face of the dress looks the same as before.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image027.jpg" alt=""></p>
<p><strong>Nevertheless, I have decided to sew wide Rigilene bones to the seam allowances of the central seam of the lining. They will start at the top edge of the dress and reach down to the level 4-5cm below the waistline.</strong> I always sew corset dresses and it really hurts to see a "wrinkled" back on this dress!</p>
<p><strong>I take two&nbsp; wide Rigilene bones of the required length and tape their ends with masking tape. </strong>The masking tape will prevent fishing lines from poking out at the ends of the bones. Otherwise they might tear the fabric and even prick the wearer.</p>
<p><strong>I put a Rigilene bone over the seam allowance of the central seam on the back side of the lining.</strong> I place the bone 1cm below the top edge parallel to the fold line of the seam allowance. I step about 1mm from the fold line so I can later fold the seam allowance together with the bone and hand-sew it to the seam allowance of the zipper.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image029.jpg" alt=""></p>
<p><strong>I attach the bone with two rows of parallel stitches. Remember to make bar tacks at the beginning and at the end.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image031.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image033.jpg" alt=""></p>
<p><strong>And then I place the other Rigilene bone onto the seam allowances of the central seam on the back side of the other half of the lining and sew it on with two rows of parallel stitches at the same distance from the waistline notch. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image035.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image037.jpg" alt=""></p>
<p><strong>As the result, the Rigilene bones are sewn in-between the seam allowances of the zipper and the central seam on the lining.</strong> These bones will reinforce the back of the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image039.jpg" alt=""></p>
<p><strong>Now I need to sew down the central seam of the lining.</strong></p>
<p><strong>I start at the notch that marks the slit.</strong> I put the edges together and pull it out through the bottom of the dress. I have no idea how I would align all these pieces without the notches!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image041.jpg" alt=""></p>
<p><strong>I secure the seam with pins.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image043.jpg" alt=""></p>
<p><strong>I make a row of joining stitches with a 2cm seam allowance starting at the notch of the slit.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image045.jpg" alt=""></strong></p>
<p><strong>I stop about 1cm before the end of the zipper and make a bar tack.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image047.jpg" alt=""></p>
<p><strong>Next, I select the largest stitch length and sew down the central seam of the lining so I can first press it open and then remove the stitches.</strong> I stop when I reach the bone.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2024_files/image049.jpg" alt=""></p>
<p><strong>The central seam of the lining looks like this when sewn down:</strong></p>
