<h2>Tutorial 11. Sewing the Lining to the Face of the Back.</h2>
<p></p>
<p><strong>We need to make a row of stitches along the pinned area from the lining side, turn it around, make a clean-edge finish seam, trim the seam allowance and then join the pieces along the armscye.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Let us begin.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>It makes sense to notch the seam allowance. This will help spread the seam and turn it out.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I direct the seam allowance towards the lining, carefully spread the seam and stitch at a 1mm distance from the edge. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Make sure to test the quality of the seam: </strong>it should be even and the seam allowances should be directed towards the same side. Don't be lazy to rip it up and re-stitch it if something looks wrong. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowances.</strong> It's best to leave the extension of the zipper tape as is. You can always trim it later. I always leave it as is at this stage, just in case. I highly recommend you notch out any excess of fabric along the princess seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>As the result the seam looks like this.</strong> Now we can press it and turn it out.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to join the face with the lining in the armscye.</strong> If you prefer sewing shoulder straps right into the corners at this stage - feel free to do so. As for me I�m not a fan of this method because it limits further adjustment opportunities. I usually attach shoulder straps to the dress at the very last moment. </p>
<p><strong>I pin the pieces together in the armscye area.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I join them. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now I can do the clean-edge finish.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Next I trim the seam allowances. There's a sharp corner left. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>Don't expect to preserve that sharpness when you turn the corner out!</strong> There is a fair amount of fabric so even if you notch it out really close to the seam the corner will still end up somewhat rounded when you turn it out. I have long given up bothering with these corners. I simply make horizontal stitches to separate a tier about as wide as the shoulder strap. My shoulder strap will be 1.2-1.5cm wide when finished, so I make a 1.5cm wide tier. There�ll be no problems turning out the corner! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image034.jpg" class="img-responsive center-block"></p>
<p>Make sure the vertical bone does not get caught in the stitch! <strong>I fearlessly trim the corner and notch out the excess of fabric.</strong> Next I do the same from the other side of the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Once you are sure the corners are placed at the same height, you can turn the back out and press it. </strong></p>
<p><strong>After turning it out you can sew the seam allowance of the lining to the zipper tape to give it a clean finish on the inside. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the lining in place to sew it to the zipper tape.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>Then I turn the dress out through the side seam opening, align the seam allowances of the central seams of the face and the lining, and carefully align their waistline and hip line notches.</strong> The key control points are the notches made on the batiste. I pin the pieces together.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I leave the central seam open and unpinned for now.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>Next I pin it down the same way from the other side.</strong> I turn it around, align the notches, and secure it with pins. If the face of the garment starts bulging, I push the air out. It is not supposed to bulge in large areas though: it only happens because of the clean-edge finish seam.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I prefer joining the pieces by stitching next to the very first row of stitches I made to attach the zipper tape when installing the zipper.</strong> In this case the seam that attaches the lining to the zipper tape will be made at a 0.5cm distance from the teeth of the zipper.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2011_files/image054.jpg" class="img-responsive center-block"></p>

