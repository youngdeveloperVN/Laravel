<h2>Tutorial 20. Finishing the Top Edge of the Front of the Dress.</h2>
<p></p>
<p><strong>Let's join the face and the lining by machine-stitching.</strong> I use a right-toe presser foot for this purpose. This presser foot allows me to move over the top horizontal bone with the needle getting right into the stay-stitch 1mm above it. <strong>Remember to make bar-tacks at the intersections of stay-stitches. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I remove the pins and carefully check the quality of the stitch.</strong> The feed dog of the sewing machine might sometimes tear the thread when passing over a pin, which results in a hole that's really hard to stitch up. You can use a magnifying glass for better control over the quality of the stitch.</p>
<p><strong>I have found a spot where the thread got torn.</strong> It looks serious! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>You cannot ignore something like this! I'll have to re-stitch it. </strong>I urge you to carefully study each spot that had a pin in it. Don't be lazy to re-stitch it if you notice anything suspicious! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to trim the seam allowances and notch out the corner of the neckline.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Here is a clever trick:</strong> add a drop of Gutermann fabric glue in the corner (above the row of joining stitches on the face, and on the lining you can smear the very stitches with it). Then you can safely notch the corner out as deep as you want. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>This fabric glue dries rather fast. It will set while I'm trimming the seam allowances and then I will handle the corner further.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowances at an angle. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I try to notch out extra fabric on the seam allowances of the princess seams.</strong> That extra fabric will make it harder to turn the garment out by adding to the thickness and forming bumps. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I reach the corner of the neckline.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image042.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I can start working on the corner.</strong> The glue has already set. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>I cut through the middle of the corner as deep as possible, basically right to the joining seam.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully notch out the seam allowance in the very corner as close as possible to the seam.</strong> I doubt I could've cut it deeper. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image047.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the excess of seam allowances on the other princess seam as well.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image049.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image051.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the corners of all other seam allowances as much as I can.</strong> Despite all your efforts don't expect that the corners of the dress will look perfect when you turn it out. It is still very thick because there are so many layers: bones, seam allowances, iron-on fabric, etc. Still, do try to trim the corners of the seam allowances as much as you can.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image052.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I turn it out.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image056.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>The corner of the neckline looks really nice!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image060.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>I need to carefully press it all now.</strong></p>
<p><strong>I take the largest cup pressing mould - it is the best one for pressing the top of the cups.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image064.jpg" class="img-responsive center-block"></p>
<p>You can pin the layers together at the sides to pull the face fabric on properly. </p>
<p>As I was pinning the layers together, I noticed that the under-bust notches on the face are not aligned with those on the lining. <strong>Oh no! It struck me that I had forgotten to sew down the darts below the bust on the face of the garment!</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>Well, what can I do now. I will finish pressing the garment and then correct my mistake.</strong> All is not lost!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image068.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image070.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image072.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image074.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image076.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image078.jpg" class="img-responsive center-block"></p>
<p><strong>The corner of the neckline looks so lovely!</strong> It is really neat thanks to that padding polyester and iron-on batiste we placed underneath it.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>I have tried to re-distribute the right under-bust dart that I forgot to sew down using the entire side seam.</strong> And it has worked out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image082.jpg" class="img-responsive center-block"></p>
<p><strong>Now I do the same from the left side.</strong> Of course this can only work with rather soft and loosely-woven fabric that's easy to press. Such is the crï¿½pe I'm using - pliable, soft and easily shaped under steam pressure. This wouldn't work with dry-clean fabric like taffeta for example! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image084.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image086.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image088.jpg" class="img-responsive center-block"></p>
<p><strong>I press it.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2020_files/image090.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to make a row of machine stitches with a 2cm seam allowance down to the hip line and then serge the side seam of the front with an uninterrupted row of stitches down to the hip line (same as for the back) and serge the side seam of the skirt separately for the face and the lining. </strong></p>
<p>
