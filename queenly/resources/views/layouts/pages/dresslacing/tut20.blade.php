						<p><strong>Features:</strong></p>
<ul>
<li>Classic pencil-dress. Modern and trendy design fits any body type perfectly!</li>
<li>Working with patterns for plus-sizes: US 12, EU-44 and larger (bust circumference 100+ cm and larger).</li>
<li>Hidden lacing.</li>
<li>The pattern included can be used for making &nbsp;a light cocktail dress as well.</li>
<li>Patterns of bolero jacket and shoulder cape included in the course.</li>
</ul>
<p><strong>Skills you gain:</strong></p>
<ul>
<li>Calculation and alteration of the plus-size patterns.</li>
<li>Sewing cups for large size bust.</li>
<li>Making hidden lacing and covering it under the blind&nbsp;zipper.</li>
<li>The technique of sewing a whole-cut dress.</li>
</ul>
<p><strong>Where to use:</strong></p>
<ul>
<li>Sewing wedding and evening dresses for non-standard body type.</li>
<li>Sewing light cocktail dresses with out corset base.</li>
</ul>
