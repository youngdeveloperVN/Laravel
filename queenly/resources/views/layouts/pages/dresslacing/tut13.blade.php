<p><strong>Here�s what I plan to do next.</strong> I will sew wide vertical Rigilene bones onto the side pieces of the front and sew vertical bones onto the central front piece (save for the central bone) while the princess seams are still open. This way it will be much easier to overlay the bones with iron-on batiste later. Besides it is also easier to work with fully finished pieces when you're sewing down princess seams. </p>
<p><strong>It is compulsory to make stay-stitches along the top of the pieces and press the wide Rigilene bones straight before sewing them on.</strong> Do not neglect it! It�s very difficult to work with pieces that have curved uneven bones sewn onto them. </p>
<p><strong>I make the stay-stitches. </strong>&nbsp;</p>
<p><strong>The central front piece: </strong>the intersection of the stay-stitches made along the top of the piece marks its centre precisely. This will come in handy later.&nbsp;&nbsp; </p>
