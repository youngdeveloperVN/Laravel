<h2>Tutorial 13. Finishing the Slit at the Back.&nbsp; </h2>
<h2>Working on the Front of the Dress. </h2>
<p></p>
<p><strong>We need to finish the slit - that open area in the central seam at the back.</strong> What does it mean? It means, we need to neatly frame it with the lining. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image002.jpg" class="img-responsive center-block"></p>
<p>Keep it in mind that it will be quite difficult to sew down the side seams if we finish the slit now because there is too little space between it and the side seam. On the other side if we first sew down the side seams, it will be hard to finish the slit because the dress will be fully assembled, heavy and awkward to work with. It's a lot easier now when you can still carefully align the edges of the slit, etc. Always weight such things up. In this particular case I prefer finishing the slit before sewing down the side seams. </p>
<p><strong>I move the back pieces away through the side seam opening and align the notches that mark the beginning of the slit on the main fabric and on the lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I pin together the layers of the slit moving down from the notch.</strong> I won�t stitch down to the very bottom of the dress but leave 6-7cm so I can trim the lining and hem the edge later. You can always sew that small area down later if necessary. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I align the notches from the other side of the slit the same way...</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>...and stitch along the pinned area.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Later, when applying final touches to the dress, you can make a clean-edge finish seam along the slit and press it. </strong>You don't have to do that though. The piped edge that we've got now looks good enough.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>The back is finished.</strong></p>
<p><strong>Now we can move to the front of the dress.</strong> Of course we will start from sewing bones onto the lining and working on the cups.&nbsp; </p>
<p><strong>But first let me secure the stiff iron-on fabric with an additional row of stitches along the perimeter. </strong>&nbsp;I make strengthening stitches along all edges of the side front pieces <strong>except for the side seam edge</strong> and along the entire perimeter of the central front piece. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2013_files/image022.jpg" class="img-responsive center-block"></p>
