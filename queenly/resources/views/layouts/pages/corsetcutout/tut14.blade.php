<p><strong>We need to place arched (gathered) Rigilene bones along the top and bottom edges of the cups. </strong></p>
<p><strong>It turned out that the location of the bone at the top edge is marked slightly wrong </strong>so I need to adjust it a little. I spread the cup with my fingers and bring the bone to the desired spot. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>The same thing happened at the bottom so I place the bone a little higher than expected. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>The bones on the second cup should be symmetric. The shape of the cups depends on this symmetry. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I sew a bone along the small arched section: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Let me remind you </strong>that all inner bones define the look of the central front piece with the fancy cut-out.<strong> Don�t pull or gather anything from the outer side </strong>or else you might deform the lining. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>Next we need to secure the bones with the second parallel stitches. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I have noticed that two parallel bones at the edges crumple the lining. And so I�m putting another bone between them to spread the fabric. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2014_files/image036.jpg" class="img-responsive center-block"></strong></p>
