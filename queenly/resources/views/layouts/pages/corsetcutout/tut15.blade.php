<h2>Tutorial 15. Assembling the Face of the Central Piece. Pressing the Lining. </h2>
<p></p>
<p><strong>You need to make stay-stitches before joining the pieces. </strong></p>
<p><strong>The stay-stitch on the side cup piece should go along the outer arched section at a 1.2cm distance from the edge (see below). </strong>I leave a tip of thread so I can slightly gather the cup later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2015_files/image002.jpg" class="img-responsive center-block"></p>
