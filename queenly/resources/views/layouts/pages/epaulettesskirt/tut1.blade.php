<h1>Epaulettes. Function and Materials. </h1>
<p></p>
<p><strong>I want to teach you how to make epaulettes.</strong> </p>
<p><strong>Epaulettes are fancy shoulder boards of a special shape with a visible fastener on the outside.</strong> They are military marks of distinctions worn by army and navy officers, generals, and admirals in Russia and in other countries. Epaulettes eventually became part of military ceremonial dress items worn on parade. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Today, they are widely used as a statement fashion accessory</strong> and often feature feathers, beads, chains, studs, bugle beads, sequins, seed beads, and fringes. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>My epaulettes</strong> will be embellished with seed beads, rhinestones, and short bugle beads. The base will be black felt fabric and I will also use some chains. </p>
<p><strong>Let me list the required materials.</strong> </p>
<p><strong>Golden Czech seed beads, silver lined inside:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Short bugle beads:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Black seed beads with golden iridescent finish:</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Chains.</strong> They can be purchased in rolls or per metre. I have chosen a Persian weave chain but you can choose any other style. You can also combine several types of chains as long as their shades match. </p>
<p>I cut such chains with regular scissors. Don't use good dressmaker's scissors for this purpose because they will definitely go blunt. </p>
<p>There is a closed link at the end of the chain and it's easy to put a thread through it. I smear the other end with glue so I can also catch it with a thread and sew it to the epaulette. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Rhinestones</strong> by Asfur Kristal. I have chosen two kinds in smoky brown and golden hues. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Epaulette pattern:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Felt fabric</strong> for the base of the epaulette: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image024.jpg" class="img-responsive center-block"></p>
<p>The felt fabric does not require any special treatment in this case: it has the right color and it is dense enough to hold its shape. I will simply cut it two epaulettes after the pattern. </p>
<p>You also need <strong>a piece of chalk.</strong> It is easy to erase and very noticeable on black. </p>
<p><strong>Glue.</strong> You can use some Gutermann fabric glue or the E6000 adhesive. </p>
<p><strong>Thread:</strong> black Tex 45 polyester-linen thread.&nbsp; </p>
<p><strong>Scissors.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I always make a test sample before working on the garment.</strong> I take a small piece of fabric and embroider it just to see how itï¿½s going to look. </p>
<p><strong>I have made a test sample before working on the epaulettes, too. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%201_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p>At this stage I can still change something: for example, decide that I prefer to use a different kind of seed beads, etc. I have tried to sew chains and other accessories to the felt fabric to see whether they match each other well enough and whether they will allow me to fulfill my design concept. I am happy with the results: I am basically using just two shades and it works great. The embroidery creates exactly the effect I want it to. </p>
<p><strong>I have approved the materials and the design concept and now I can safely get down to work.</strong></p>

