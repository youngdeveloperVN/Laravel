						<p><strong>Features: </strong></p>
<ul>
<li>Combination of two sewing technologies: traditional one-layer (transparent) corset + Throw-over technique.</li>
<li>Pattern with vertical front reliefs and modern round separately cut cups consisting of three parts.</li>
</ul>
<p><strong>Skills you gain:</strong></p>
<ul>
<li>Marking boning in separately cut cups consisting of three parts;</li>
<li>Adjusting the size of this cup;</li>
<li>A new way of treating the front part of the cup;</li>
<li>Assembling chequered pieces in the Throw-over technique;</li>
<li>Special features of cutting and assembling garment from chequered pieces of fabric.</li>
</ul>
<p><strong>Where to use:</strong></p>
<ul>
<li>Widely used technology for creating wedding (and evening) dresses, and also for youth fashion corsets.</li>
</ul>
