<h3>Tutorial 10. Joining the Skirt Pieces.</h3>
<p></p>
<p><strong>First I need to assemble the lining of the skirt.</strong> </p>
<p><strong>The lining of the back:</strong> I assemble each half of the back separately, i.e. without sewing up the central seam. You shouldn't forget that the princess seams are longer than the side seams so it's important to match the pieces correctly. You can use the shiny side as the face of the lining for a prettier look. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2010.files/image002.jpg" class="img-responsive center-block"></p>
