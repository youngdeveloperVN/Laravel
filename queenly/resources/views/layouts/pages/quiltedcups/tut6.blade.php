<h3>Tutorial 6.&nbsp; Sewing-on Vertical Bones.</h3>
<p></p>
<p>I have quilted round corset cups and now I start sewing-on vertical bones.</p>
<h4>Front Piece.</h4>
<p></p>
<p><strong>I continue working on the front piece of the lining.</strong></p>
<p><strong>I start by sewing a vertical bone onto the left medium piece of the front (bone 12 after the marking). </strong>I take a narrow Rigilene bone, tape its end over with masking tape and place it at the marking line so that the marking goes along the middle of the bone. I step aside (by 2-3mm) from the stay-stitch going along the bottom of the corset.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image002.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image538.jpg"></p>
<p>I reach the rounded bone under the bust without overlapping it and make the second strengthening stitch: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I attach another bone along the curve exactly the same way stepping aside from the stay-stitch:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p>I try to make the joining seam go along the middle of the bone.</p>
<p></p>
<p>Now I go on to the central piece of the front.</p>
<p><strong>I sew-on the first side vertical bone (bone 4 after the marking):</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image007.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I stitch-on the central vertical bone (bone 1 after the marking):</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image009.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I stitch-on the second side vertical bone (bone 3 after the marking):</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image011.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Another vertical bone goes along the second curve:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I sew a vertical bone onto the right medium piece of the front (bone 14 after the marking):</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image013.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Please take a look at what the front piece looks like after all vertical bones have been stitched-on:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image015.jpg" class="img-responsive center-block"></p>
<h4>Back Piece.</h4>
<p></p>
<p><strong>I start sewing bones onto the back pieces of the lining.</strong></p>
<p>I take the first sewn back piece consisting of the central and side pieces of the back and a side piece of the front. </p>
<p><strong>I attach a wide Rigilene bone to the central piece of the back</strong> 1mm from the stay-stitch. I leave some 2-3mm space from the top and bottom of the bone. I leave some room for the lacing.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image017.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image553.jpg"></p>
<p><strong>And I sew-on the second parallel wide bone just the same way:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image019.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I sew-on the third parallel bone:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image021.jpg" class="img-responsive center-block"></strong></p>
<p>Having attached these 3 vertical bones to the central piece of the back I have created a base for a beautiful hard lacing of the corset. </p>
<p><strong>As a next step I sew a wide Rigilene bone onto the side seam:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image023.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And then onto the back curve:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image025.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I sew a narrow Rigilene bone onto the side piece of the front:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image027.jpg" class="img-responsive center-block"></strong></p>
<p>I sew-on this bone following the marking line.</p>
<p><strong>A small tip: </strong>fabric gets deformed during the sewing and pressing processes and marking lines might turn into wiggly lines instead of staying straight. Do try to arrange fabric with your fingers when sewing-on bones to return the line to its original state and secure it with the bone. These bones are sewn-on exactly for this purpose. </p>
<p>Thereby I not only secure the corset vertically but bring the fabric fiber back to its original undeformed shape. </p>
<p>If the level of deformation is quite serious, you should first of all stitch-on the in-between bones and only afterwards go on to the bones of the curves and the side seam.</p>
<p><strong>Please note that vertical bones can be either wide or narrow. You can make your own choice according to your experience and the design. Horizontal bones must be narrow! </strong></p>
<p><strong>I sew-on another narrow Rigilene bone like this following the marking on the side piece of the back:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%206_files/image029.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I sew the same kind of bones and in the same sequence onto the second back piece of the lining joined along its curves. </strong></p>
<p>To sum it up, in this tutorial I have sewn-on all vertical bones of the corset lining.</p>

