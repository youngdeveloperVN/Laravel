<h3>Tutorial 10. Cutting the Upper Part of the Skirt. </h3>
<p></p>
<p><strong>I have already put the dress back on the dress-form.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Now the key task is to make the location of the side seams as precise as possible, i.e. place them on the side surfaces of the dress-form. </strong>This will balance out the tension at the front and at the back.&nbsp; </p>
<p>I have pinned down the central part of the front half to secure it in place as well as the bottom of the panties to balance the tension vertically. Now I can see what the garment will look like when the side seams are sewn down. </p>
<p><strong>I fold the top edge of the upper skirt piece by 1cm and secure it by stitching.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p>It's best to use two-way stretch fabric so you can pull it onto your dress-form; and it should stretch widthwise (crosswise in this case). <strong>I fold the fabric in half, mark the centre, and match it with the centre marked on the waistline of the dress-form.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>It is very important to place the fabric with its lengthwise grain strictly parallel to the vertical line marked on the dress-form.</strong> In order to do this I mark the lengthwise grain direction with a marker after folding the fabric in half and then pin this central lengthwise line right to central vertical line of the dress-form holding the fabric up to light. <strong>I pull the top edge of the piece on along the waistline and pin it down.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the dress-form over and bring the top edge of the skirt to the point marked at the back.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I spread the fabric and pin the ends together so as to form a downward-looking corner in the middle.</strong> I mark the spots where the top edges of the skirt meet at the back.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>After you spread the fabric at the back and all wrinkles at the sides, you will see that it's a bell skirt whereas what we want is a straight cut or even slightly narrowed skirt.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>We need to adjust the sides to straighten the skirt.</strong> I make a dart, pin it, and mark it on fabric for that purpose. What I get is a really deep dart. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I want the upper part of the skirt to be 45cm long along the side seam</strong>; so I mark that value. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>It is supposed to be 55cm long along the central seam from the waistline down (the dart included).</strong> I mark it, too.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>The length of the front half of the upper piece should be 35cm along the central vertical line.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I have basically marked all we need - in other words I have drawn a pattern of the upper part. </strong></p>
<p><strong>I take the fabric off the dress-form and fold it in half.</strong> We will need to make the dart converge at a single point.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I add some space for the seam allowances and cut out the piece. </strong>The seam allowance for the central seam where we will insert the zipper and the train is 2cm and the one along the bottom of the skirt is 1.2cm.</p>
<p><strong>In order to make the dart converge I will gather the fabric in the area that starts 4cm above the dart and finishes 10cm below it. </strong>I make notches at the beginning and at the end.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>You could make two parallel stitches between the notches and gather it down to the length of 14cm, which is quick and easy, but you can also make symmetric tucks.</strong> The latter is done on the dress-form to make sure the tucks are absolutely identical on the left and on the right. Then you simply secure the tucks and the gathering is ready. The resulting gathered area should be 14cm long. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2010_files/image036.jpg" class="img-responsive center-block"></p>
