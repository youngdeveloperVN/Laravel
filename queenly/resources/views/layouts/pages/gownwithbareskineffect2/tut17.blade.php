<h3>Tutorial 11. Improving the Cut of the Upper Part of the Skirt.</h3>
<p></p>
<p>I know it from experience that the top skirt will inevitably start 'jumping up' in the middle when I start attaching it to the top of the dress. We make corresponding provisions in advance. </p>
<p><strong>It is enough to shift the centre of the skirt down by 1.5cm. </strong>First I mark the location of the side seam with some allowance for fabric tension and then I rip up the area between the side seams at the top of the skirt, re-draw the top line with due account for the seam allowance, and trim it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I notch the adjusted area of the top edge, fold it and press it. Later I will secure it with a stitch along the edge. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>So I have cut out a semi-circle that will serve as the bottom flounce.</strong> Please note that I've cut the base simultaneously with the lace overlay.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Now let me share a small trick.</strong> During the cutting process I spread out the main fabric and put the lace over it. I secure them together with several drops of Gutermann glue made with 6-8cm intervals along the central back seam and the perimeter of the semi-circle. I let the glue set properly. This way the layers hold together safely and the edges of the lace don't warp the way they tend to when you make a joining stitch. This allows you to work with the base and the overlay as a single unit. </p>
<p><strong>I have done the same for the train, too.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I have already gathered an area of 14cm on the skirt by making two parallel stitches between the notches and put the skirt back on the dress-form. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the skirt down in the area of the supposed central seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>The upper part of the skirt should have a very tight fit or else the gathering won't look graceful.</strong> I can tell that I could make my skirt tighter with the help of the central seam. So I adjust the central seam, pull on the skirt, and re-adjust the pins along the central seam down to the point where we start sewing in the train. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the new location of the central seam looking to the pins.</strong> It's impossible to avoid this adjustment since we are working with highly resilient materials and it's hard to predict their extensibility.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>After re-drawing the contour of the central seam </strong>I undo the previous gathering. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the skirt in half, add a seam allowance of 2cm to the new joining line, and transfer the notches of the beginning and the end of gathering.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I trim the central cut along the seam allowance line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2011_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to make two rows of parallel running stitches from each side: the new joining seam will be inbetween.</strong></p>

