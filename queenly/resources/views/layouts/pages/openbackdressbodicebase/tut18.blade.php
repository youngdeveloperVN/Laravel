<h3>Tutorial 18. Sewing Bones onto the Cups.</h3>
<p></p>
<p><strong>I join the bottom face halves of the cups with their top parts.</strong> You should notch the seam allowances of the top parts. I pin the pieces together as indicated by the numbers marked.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image010.jpg" class="img-responsive center-block"></p>
<p>Then <strong>I trim the corners and the width of the seam allowances</strong> trying to keep them even along the entire perimeter of the cup and equal on both cups since the seam allowances will slightly show through.&nbsp; I will press the seam allowance flat upwards. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>The other cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image018.jpg" class="img-responsive center-block"></p>
