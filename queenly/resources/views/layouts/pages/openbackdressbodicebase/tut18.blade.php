<p><strong>I take a narrow Rigilene bone and gather it by pulling the fishing lines.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the gathered Rigilene bone onto the cup along its bottom edge.</strong> You should cover its ends with pieces of masking tape. The bone follows the stay-stitch and is placed 1mm below it. I ease in less before the princess seam and start easing in more as I reach it. I stop a couple millimeters before the intersection of the stay-stitches and trim the bone.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image048.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I gather another Rigilene bone into a semi-circle and lay it along the top edge.</strong> I do some light easing in to ensure better fitting of the cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image054.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image056.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image058.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to lay a bone along the bust line.</strong> The bust line basically coincides with the seam that joins the top and the bottom of the cup. I start laying the bone along that joining seam trying not to stretch the cup yet at the same time prevent the fabric from gathering underneath the bone. I finish sewing in a straight line without bending the bone after the contour of the joining seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image062.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image064.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image066.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>I make another row of parallel stitches through the bone.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image070.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image072.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Next I lay another bone along the princess seam of the cup</strong> that crosses it from top to bottom. I let the bone settle naturally inside the cup. You must have noticed that I haven't marked the location of the bones. It's because the princess seams of such cups indicate it perfectly anyway. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image074.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image076.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image078.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>Whether we should add an additional bone at the bottom of the cup will be clear after the inclined vertical bones are sewn in place.</strong></p>
<p><strong>I sew down the first inclined bone. </strong>You should push its ends underneath the perimeter bones.</p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image082.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image084.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image086.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I secure the bone with another row of parallel stitches.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image088.jpg" class="img-responsive center-block"></p>
<p><strong>And I lay down the second inclined bone the same way.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image090.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image092.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image094.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image096.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now let's see what's happening with the cup: itï¿½s getting deformed underneath the bones. </strong>&nbsp;The main challenge of lining such soft (non-duplicated) cups with bones is to prevent them from getting deformed. As you can see, there are some vertical creases in the centre of the cup. The bone at the bottom does not provide enough tension to spread the bottom part of the cup.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image098.jpg" class="img-responsive center-block"></p>
<p><strong>This means I need to lay an additional bone at the bottom.</strong> I gather the bone into a semi-circle and sew it down with two rows of parallel stitches. Again, I push its ends underneath the perimeter bones. I spread out the vertical creases making the bottom of the cup rounder. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image100.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image102.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image104.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image106.jpg" class="img-responsive center-block"></p>
<p><strong>The cups are now fully boned.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image108.jpg" class="img-responsive center-block"></p>
<p><strong>The last step is to make the second raw of parallel strengthening stitches along the perimeter bones. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image110.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image112.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image114.jpg" class="img-responsive center-block"></p>
<p><strong>Now that the bones are in place, I can fold the seam allowances inwards and stitch them down.</strong> The stay-stitch should be on the rib of the crease. </p>
<p><strong>I sew down the seam allowance at the bottom of the cup.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image116.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image118.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image120.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image122.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image124.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same with the seam allowance at the top of it.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image126.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image127.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image128.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image130.jpg" class="img-responsive center-block"></p>
<p><strong>As the result the cup looks like this:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image132.jpg" class="img-responsive center-block"></p>
<p><strong>I turn it out and trim the seam allowances.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2018_files/image134.jpg" class="img-responsive center-block"></p>
<p>
