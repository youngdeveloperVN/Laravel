<h1>Tutorial 22. Analyzing the Skirt Pattern and Ruffles Layout.&nbsp; Cutting the Skirt.</h1>
<h2>The Skirt.&nbsp; Introduction. Review of Materials Used.</h2>
<p><strong>What materials will be used for the see-through skirt of the dress? </strong></p>
<p><strong>The base will be made of soft and hard mesh fabric. </strong>The soft mesh fabric is light-beige resilient fabric with a width of 120cm. The hard mesh fabric is champagne-colored and it is a little coarser. I could not find the two fabrics in the same color.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image004.jpg" alt=""></p>
<p><strong>The main fabric is curtain fabric known as half-organza or micro veil.</strong> I will use two shades of the same color to create an iridescence effect.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image006.jpg" alt=""></p>
<p><strong>The hem of the skirt will be finished with a 2.5cm soft Rigilene boning tape.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image008.jpg" alt=""></strong></p>
<p><strong>And, of course, I will use a soldering iron to heat seal the edges. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image010.jpg" alt=""></p>
<p><strong>You can see the skirt pattern in the picture below.</strong> It is very large: what you see in the picture is the actual skirt pattern folded in half.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image012.jpg" alt="plux1-2-006 v:shapes="></p>
<p><strong>Speaking about the design, it is a typical half-circle skirt with a train at the back.</strong> The radius of the half-circle is 24cm, which corresponds with a waist circumference of about 65-66cm. The skirt is 165cm at the back and 120cm at the front. The front half of the skirt has a fold line and the back half has a seam in the middle.</p>
<p><strong>Seam allowances are not included in the pattern</strong> - remember to account for them in the cutting process. The pattern and related guidelines are included in the book.</p>
<p><strong>You can see a red line parallel to the bottom edge of the pattern. </strong>It bears a direct relation to the cutting process. As I said, the base of the skirt will be made of two layers: soft and hard mesh fabric. The bottom part of the skirt with a width of 30cm (the part of the pattern from the bottom to the red line) will be made of two layers of hard mesh fabric. Everything above the red line will be cut from the soft mesh fabric.</p>
<p><strong>The hard mesh fabric is used to</strong> make the hem of the skirt wavy while preserving its rounded shape. Additional soft Rigilene boning will make the hem stiffer and help it hold the shape.</p>
<p><strong>The rest of the lines marked on the pattern</strong> are your guidelines for sewing on the ruffles - the most valuable part of the pattern. We will talk about them a little later.</p>
<p><strong>I start cutting the skirt.</strong> The pattern is large so I work right on the floor.</p>
<p><strong>I cut the hard mesh fabric first. </strong>The fabric is folded in half. I carefully press the fold line, align it with the fold line of the pattern, and secure them with pins. I will cut the mesh with a 1.2cm seam allowance along the hem, above the red line, and along the edges of the central back seam. The fabric has a width of 185cm and a length of 4m.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image014.jpg" alt="plux1-2-007 v:shapes="></p>
<p><strong>I put the hard mesh fabric on top of the pattern and cut the piece out with seam allowances added. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image016.jpg" alt="plux1-2-008 v:shapes="></p>
<p><strong>I need to cut out two such pieces. </strong>There is a large piece of mesh fabric left. I could spend it in a more efficient way and cut the second piece with a seam in the middle. But I prefer not to spare any fabric in this particular case but have as few seams as possible because it is a premium garment.</p>
<p><strong>I take another 4m of mesh fabric, fold it in half, and put it on top of the already cut out piece to preserve the same configuration. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image018.jpg" alt="plux1-2-009 v:shapes="></p>
<p><strong>I cut the second piece following the contour of the first one. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image020.jpg" alt="plux1-2-010 v:shapes="></p>
<p><strong>Now I need to cut the soft mesh fabric.</strong> I prepare the same kind of a lay and cut the piece out adding all seam allowances. The fabric is wide enough (120cm). I haven't trimmed the selvage along the central back seam edge yet. If I do, the edge will go wavy and it will make further work a lot harder.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2022-1_files/image022.jpg" alt="plux1-2-011 v:shapes="></p>
