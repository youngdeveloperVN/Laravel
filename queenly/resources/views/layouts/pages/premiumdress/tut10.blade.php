<h1>Tutorial 9. Sewing Bones along the Front Princess Seams.</h1>
<p><strong>I secure the central bone with two rows of parallel stitches. </strong>I gather the bone in advance, push its end underneath the top horizontal bone, trim the other end as marked, and tape it over with masking tape.&nbsp; I use narrow Rigilene bones just like on the cups because the dress does not imply a strong body control effect. I recommend using 10-12mm Rigilene bones for a strong body control effect if it is what you want to achieve.</p>
