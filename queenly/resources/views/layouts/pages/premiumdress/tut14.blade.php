<h1>Tutorial 13. Sewing the Side Seams of the Lining.</h1>
<p><strong>I have done a preliminary fitting which involved putting the dress on the client and lacing it down with the help of the lacing panels sewn into the back princess seams.</strong> I have decided to widen it just a little bit at the stomach level, by just some 4-5mm, to be on the safe side.&nbsp; Apart from that, the dress fits my client perfectly. The pattern turned out to be very precise, i.e. all calculations were carried out correctly. The garment has good balance and the cups have proper length and depth.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image002.jpg" alt=""></p>
<p><strong>Now I will sew along the re-drawn sections of the side seam lines in the stomach area and rip up the previously made stitches. </strong>I rip up the central back seam to unfold the dress for the sake of convenience.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image006.jpg" alt=""></p>
<p><strong>I need to topstitch the seam allowances of the side seams</strong> down to the end of the iron-on fabric. I trim the ends of the side seam at an angle. I carefully spread and stretch out the seam during topstitching to avoid tucks and wrinkles.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image014.jpg" alt=""></p>
<p><strong>I trim the seam allowance of the side seam.</strong> You can carefully trim the seam allowances of the side seams and all other seams with a soldering iron after a fitting, as taught by Julia Trofimova. Heat-sealed edges are much thinner than serged edges.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image016.jpg" alt=""></p>
<p><strong>I trim the seam allowances on the other half of the dress before topstitching them.</strong> This makes trimming faster and easier but topstitching becomes a lot more troublesome. It is up to you which method to go with.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image026.jpg" alt=""></p>
<p><strong>There will be wide Rigilene bones going along the side seams from the top of the dress down to the end of the iron-on fabric. </strong>Such long bones at the sides will secure the skirt in place and improve the fit of the dress in the waist and in the hips. Of course, they won't eliminate wrinkles around the waist or the hips completely but they will still smooth them out considerably.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image032.jpg" alt=""></p>
<p><strong>The first bone is sewn in place.</strong> Make sure to check the quality of the seam. There should be no tucks, creases, or distorted fabric underneath the bone. The side seam is very curvy and you have to watch for the fabric to settle down evenly from either side of the bone as you are sewing it on. You could overlay this bone with lace but I don't see a big need in that.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image034.jpg" alt=""></p>
<p><strong>I sew the same kind of a bone along the other side seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image044.jpg" alt=""></p>
<p><strong>In addition, I want to make part of the central back seam with the zipper a little prettier. </strong>The seam allowance of the central back seam is 2cm. I place a lace ribbon along the central open edge at a distance of 1.5cm. I sew it on stepping precisely 2cm from the central open edge, i.e. the width of the seam allowance. I fold the ribbon to the edge when I reach the bottom zipper notch.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image048.jpg" alt=""></p>
<p><strong>I secure the ribbon with another row of parallel stitches made along its other side.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image050.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image052.jpg" alt=""></strong></p>
<p><strong>The ribbon is now sewn in place. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image054.jpg" alt=""></p>
<p><strong>If I fold back and press the edge of the central seam keeping the seam allowance exactly at 2cm, the beautiful edge of the lace ribbon will cover up the edge of the zipper. </strong>You can sew this edge of the ribbon to the zipper tape with blind stitches. It will look lovely.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image056.jpg" alt=""></p>
<p><strong>I do the same thing with the opposite central edge of the back. </strong>When the central seam is sewn, the zipper will be framed with two pretty lace ribbons.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2013_files/image058.jpg" alt=""></p>
