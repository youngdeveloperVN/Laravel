<h1>Tutorial 12. Assembling the Back Half of the Lining. Preparing for a Fitting.</h1>
<p><strong>I position the first lacing bar on the corresponding piece of the lining of the back. </strong>Be careful not to get confused and remember to put the wrong side of the lacing panel to the face side of the lining of the back. You want to see the face of the lacing panel when you unzip the dress.</p>
<p><strong>I pin the lacing panel to the edge of the dart aligning their notches and edges. </strong>&nbsp;The configuration of the sew-in edge of the lacing panel fully repeats that of the dart. The end of the lacing panel is aligned with the end of the dart.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image002.jpg" alt=""></p>
<p><strong>I fold the piece in half along the dart line and pin the raw edges together.</strong> The lacing panel stays inside. I mark the top of the dart with a pin. The sewing line is marked correctly: I can sew the dart and the lacing panel will stay inside.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image004.jpg" alt=""></p>
<p><strong>And I do the same with the other lining piece. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image006.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image008.jpg" alt=""></p>
<p><strong>Both back princess seams are ready to be sewn. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image010.jpg" alt=""></p>
<p><strong>I sew the first back princess seam...</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image012.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image014.jpg" alt=""></p>
<p><strong>...and then the second back princess seam.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image016.jpg" alt=""></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image018.jpg" alt=""></strong></p>
<p><strong>I remove the pins and topstitch the seam allowance folding it to the side of the dress.</strong> It will be easier to sew a bone along the princess seam this way. You can first press the seam allowance flat if you wish.&nbsp; I stop stitching at the end of the guiding line - 14cm below the waistline.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image020.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image022.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image024.jpg" alt=""></p>
<p>I fold the seam allowance to the side of the dress despite all sewing rules (you usually topstitch it folded toward the center of the dress). Since the same princess seam secures the lacing panel on the dress, it will be way too thick if I topstitch it folded toward the center. <strong>And for this reason I fold it toward the side of the dress. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image026.jpg" alt=""></p>
<p><strong>I topstitch the seam allowance of the other back princess seam directing it to the side of the dress.</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image028.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image030.jpg" alt=""></p>
<p><strong>I only trim the corners of the seam allowances of the back princess seams because it will be easier to sew on bones if they are wide enough.&nbsp; </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image032.jpg" alt=""></p>
<p><strong>Remember to make a row of stay-stitches along the top edge of the back pieces before sewing on the bones.</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image034.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image036.jpg" alt=""></p>
<p><strong>I have chosen wide Rigilene bones for the back of the dress.</strong> I sew them on according to the guiding lines stepping 2-3mm down from the row of stay-stitches. I press the bones in advance.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image038.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image040.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image042.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image044.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image046.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image048.jpg" alt=""></p>
<p><strong>The back is now reinforced with bones. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image050.jpg" alt=""></strong></p>
<p><strong>I overlay the bones with lace ribbons just like I did on the front half of the lining.</strong> To make sure the bottom decorative trimmings will be placed at the same level, I align the side seam lines of the back piece and the corresponding front piece and mark the point where the trimming is supposed to arrive on the side seam line of the back piece.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image052.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image054.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image056.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image058.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image060.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image062.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image064.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image066.jpg" alt=""></p>
<p><strong>The first back piece is finished and I do the same with the second piece. </strong>Now I need to press the princess seam and the sewn-on bones together with the decorative lace ribbons.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image068.jpg" alt=""></p>
<p><strong>Now I need to sew the side seams of the dress.</strong> I put together the lining pieces of the front and the back, align their side edges, secure them with pins at the notches, and join them with a 2cm seam allowance.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image070.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image072.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image074.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image076.jpg" alt=""></p>
<p><strong>And I sew the other side seam.</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image078.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image080.jpg" alt=""></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image082.jpg" alt=""></p>
<p><strong>The side seams are sewn and the decorative ribbons meet neatly. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image084.jpg" alt=""></strong></p>
<p><strong>Now is a good time for a fitting.</strong> It is better to examine the garment on your client now than realize you have to correct possible cutting or sewing mistakes after sewing bones along the side seams.</p>
<p><strong>Carrying out a fitting requires that you sew the central back seam from the zipper notch down to the bottom of the dress.</strong> I recommend you use large stitches without bar tacks because you will need to rip the seam up and unfold the dress again afterwards to continue working on the side seams (add the bones, overlay them with lace ribbons, and press them).</p>
<p><strong>I sew the central back seam starting from the bottom zipper notch.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image086.jpg" alt=""></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2012_files/image088.jpg" alt=""></strong></p>
<p><strong>The dress is ready for a fit test.</strong></p>

