<p><strong>I am approaching the other side seam. </strong>I want the edge of the mesh fabric to settle upon the side seam because it is the armscye area. I fold the edge and pin it on along the side seam. This area will be decorated by large ruffles: you will only be able to see the drapes at the top so there is no need in forming closely spaced drapes in the middle.&nbsp; </p>
