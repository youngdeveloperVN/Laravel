<h1>Tutorial 20. Draping the Bodice.</h1>
<p></p>
<p><strong>I have put the dress on the dress-form to drape the bodice.</strong> I recommend using lightweight foam plastic dress-forms for such tasks. It is okay if your dress-form doesn't quite correspond with the size of your dress. Mine, for example, is larger than the dress but I have simply laced it up and secured the zipper tape with pins to help it hold. Although the side seams are not positioned correctly on the dress-form, I can still use them as guidelines because they show where my client's sides are. If your dress is a lot larger than the dress-form, simply stuff it with foam, padding polyester, or other similar stuffing material. The dress-form needs to be lightweight and mobile so you can easily turn it and move it around the way you need. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I marked the bottom border of the belt during the fitting and now I mark it additionally with a row of stitches all the way round. </strong>It will help me remember that the drapes should stop just above this line. The skirt will also be attached just above it. </p>
<p><strong>It is best to overlay the ends of the drapes even though they will be covered up by the belt.</strong> I will use a nude elastic band for this purpose.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>The bodice will be draped with nude mesh fabric.</strong> (Remember to trim away the selvage).</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Let us begin. </strong>According to the design concept, there is a shoulder strap at the front of the dress which starts on the left and goes up diagonally. It meets the same kind of a diagonal strap going up from the back. The two straps are then tied together in a bundle on the shoulder.&nbsp; </p>
<p><strong>I will start from the right half of the back and keep arranging new drapes in the round until I reach the left half. </strong>There is no shoulder strap on the right so I simply form regular drapes. Make sure you can fold the draping fabric over the edge of the back neckline.&nbsp; </p>
<p><strong>I fold the vertical edge of the draping fabric, position the first drape at an incline on the right half of the back </strong>(thereby setting the draping direction), and secure it with pins at the top, down the bottom, and in the middle.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I start forming small drapes in the waist area and securing them with pins.</strong> My guiding line is the row of stitches which marks the bottom border of the belt. I adjust the depth of the drapes in the waist area depending on how well they settle upon the top part of the back and secure them with pins at the top, down the bottom, and in the middle. I pull the mesh fabric on to keep the drapes taut. My drapes have a depth of about 1cm.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image014.jpg" class="img-responsive center-block"></p>
