<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I reach the front of the dress.</strong> Remember to account for the prominence of the bust. And don't forget that you are supposed to start forming the shoulder strap after passing the side seam.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the mesh fabric on the draped half of the back stopping at the side seam. Drapes formed after the side seam are supposed to go upwards and over the shoulder.</strong> I trim the mesh fabric leaving some space for folding it over the neckline edge. And I pull the rest of it in the required direction (toward the shoulder) and secure it with pins. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2020_files/image024.jpg" class="img-responsive center-block"></p>
<p>
