<h1>Tutorial 5. Preparing for the Cutting Process.</h1>
<p><strong>I put the lining fabric and the sequin fabric on the table,</strong> align their edges, and pin them together. <strong>I then put the pattern transferred onto iron-on batiste on top of the lay</strong>, aligned with the fold line, and secure it with pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%205_files/image002.jpg" alt=""></p>
<p><strong>I mark the position of the front side seam line. </strong>The hip line is lowered by about 4cm on this dress. For this reason, the side seam is also supposed to continue 4cm below the hip line. I step 4cm down from the hip line on the front skirt piece, add a seam allowance of 1.2cm, and align the top front piece with the corresponding skirt piece. I pin the skirt piece to the lay.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%205_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%205_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%205_files/image008.jpg" alt=""></p>
<p><strong>Next, I put down the paper pieces related to the back of the dress. </strong>I position them in the opposite direction to the front pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%205_files/image010.jpg" alt=""></p>
<p><strong>I have calculated fabric consumption in advance</strong> by arranging the patterns on the cutting table and measuring the width of the lay with some extra centimeters added just in case.</p>
<p><strong>In my case, it will take 2m of fabric </strong>to make this dress slightly flared at the sides (12cm) and in the central back seam (20cm).</p>
<p><strong>I take a marker and draw a train starting from the hip line and aligning the top of the back piece with the skirt. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%205_files/image012.jpg" alt=""></strong></p>
<p><strong>I got some doubts about using the sequin fabric during the cutting.</strong> I have noticed that the sequins get pulled apart when you pull on the fabric a little, and it becomes see-through. This might result in a light difference in the color of the bodice (where sequin fabric is glued onto white batiste) and the color of the bottom part of the dress (where sequin fabric is set against a contrasting background).</p>
<p>Besides, I am not sure the batiste will adhere well enough to the wrong side of the sequin fabric.</p>
<p><strong>I don't want to waste expensive fabric in vain so I will just add another layer of lining fabric.</strong></p>
<p>The additional lining layer will be just a couple of centimeters longer than the iron-on batiste (i.e. I won't cut out the entire skirt).</p>
<p>Instead of fusing the batiste onto the beautiful sequin fabric, I will use it to duplicate the lining pieces and then join each piece with its sequin fabric overlay.</p>
<p><strong>Having made the decision, I put an additional layer of lining fabric under the iron-on batiste in the lay of the top part of the dress. </strong>In other words, I will cut out two additional lining pieces for the face of the bodice apart from the main lining of the dress.</p>
<p><strong>As the result, I have prepared a lay of five layers of fabric and I will cut:&nbsp; </strong></p>
<p>-&nbsp; Lining pieces in fours: long pieces in twos for the seamless part of the dress and short pieces in twos, a couple of centimeters longer than the iron-on batiste.</p>
<p>- Sequin pieces in twos, including the skirt of course.</p>
<p>- And iron-on batiste pieces in fours.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%205_files/image014.jpg" alt=""></p>
<p><strong>I still need to cut duplication pieces for the lacing panels, too.</strong> I prepare a lay of four layers of batiste and four layers of lining fabric and pin the lacing panel pattern onto it. Don't forget to mark the top line of the piece and transfer all notches.</p>
