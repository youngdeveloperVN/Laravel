<h1>Tutorial 40. Summing Up the Results. Related Materials Consumption. </h1>
<p></p>
<p><strong>Here is the skirt in all its beauty! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2040_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I just need to adjust its length at the front.</strong> This involves doing another fitting. I help the client put the skirt on and secure it in place with pins. I wrap a wide elastic band tightly around the top edge of the skirt right on the client. Then I remove the pins at the front and pull the mesh fabric up from behind the elastic band to achieve the right length. (Ask your client to walk around to determine the length more precisely). When you find the right length (which should be comfortable and not make your client stumble), mark a line along the top edge of the elastic band to remove the excess of mesh fabric. Then, arrange the skirt on the table and adjust the marked line to achieve a smooth semi-circle waistline. This is the fastest and the most precise way of adjusting the front length of such garments. </p>
<p><strong>If you need to adjust the length at the back as well, </strong>then simply pull the mesh fabric up and out from behind the elastic band all along the perimeter of the skirt. </p>
<p><strong>You can use the skirt as an individual garment or sew it to the dress.</strong> You can even make a transformer dress with a removable skirt fastened by an elegant belt, etc. This way your client will have two outfits at once: a gorgeous dress with a designer skirt for the festive ceremony or the photo-shoot and a simpler yet no less beautiful dress (without the skirt) for the rest of the party. </p>
<p><strong>I would like to draw your attention to the hem of the skirt again.</strong> Although the Rigilene bone makes the hem somewhat coarser, it also makes it a lot comfortable for the wearer to move around. The skirt will have softer falls without that bone, which is great for a photo-shoot. You should discuss it with the client and choose the best option. It is not hard to rip off the bone because the last ruffle is sewn on higher. The bias tape will stay in place: you will simply need to remove excess thread. It won't take longer than 10 minutes of work if you do decide to sew on this bone.&nbsp; </p>
<p><strong>Let me share another clever trick with you.</strong> The sewn-on bias tape forms a tunnel. And this tunnel is perfect for inserting a synthetic whalebone. There is no need in sewing a Rigilene bone along the bottom edge of the skirt in this case. Moreover, you can carry out a photo-shoot without the whalebone and then simply insert it in the tunnel to make the skirt fuller afterwards. </p>
<p><strong>After this impressive amount of work, the skirt is finally ready. Let us see how much fabric and other materials it has taken. </strong>&nbsp;</p>
<p><strong>I have used: </strong></p>
<p>- 16m of light half-organza with a width of 3m; </p>
<p>- 12m of dark half-organza with a width of 3m; </p>
<p>- 12m of hard mesh fabric with a width of 180cm;&nbsp; </p>
<p>- 4m of soft mesh fabric with a width of 120cm.&nbsp; Another 2m was used for the dress itself. </p>
<p>- 6m of 2.5cm soft Rigilene boning tape and the same amount of 10cm soft Rigilene boning tape, Rigilene bones, and bias tape.&nbsp; </p>
<p>After cutting out the ruffles, there were several triangular pieces of half-organza left at the beginning and the end of the lay. <strong>I will use them to create a fabulous decorative shoulder piece for the dress. </strong></p>
