<p><strong>The zipper is now in place and the two halves of the back are located at the same level.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image026.jpg" alt=""></p>
<p><strong>I finish sewing it in with the help of an invisible-zipper presser foot.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image030.jpg" alt=""></p>
