<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image091.jpg" alt=""><strong>I trim the seam allowances of the side seams on the backing pieces and the sequin pieces down to a width of 1cm starting from the waistline and up. </strong>Now I only need to press the side seams.</p>
