<p><strong>I join the backing pieces by sewing the side seams with 2cm seam allowances. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image085.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image087.jpg" alt=""></p>
