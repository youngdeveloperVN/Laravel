<h1>Tutorial 16. Installing the Zipper. Sewing the Side Seams of the Face.</h1>
<p><strong>I need to prepare the pieces for sewing in the zipper.</strong> I put the face and the backing of the back piece together, align their side edges, and secure them with pins. The face piece shrank by about 0.5cm widthwise during the pressing (or maybe the sequins made the cutting process a little less precise). I pin the pieces together along the central back seam. When sewing in the zipper, I will mark 2cm seam allowance from the edge of the piece cut from the backing fabric. The same thing needs to be done with the other piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image002.jpg" alt=""></p>
<p><strong>I join the first pair of pieces by stitching at a presser-foot width from the central edge, from the top down to the notch that marks the end of the zipper. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image006.jpg" alt=""></p>
<p><strong>Then I take the other pair of pieces and start sewing from the notch that marks the end of the zipper.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image010.jpg" alt=""></p>
<p><strong>Next, I sew the zipper tape to the 2cm seam allowance.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image012.jpg" alt=""></p>
