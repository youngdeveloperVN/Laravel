<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image034.jpg" alt=""></p>
<p><strong>Done!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image036.jpg" alt=""></p>
<p><strong>I can sew the central back seam now. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image038.jpg" alt=""></p>
<p><strong>I will sew the central back seams separately on the face and on the backing.</strong> I switch to a left-toe presser foot and turn out the backing to move it out of the way. If your fabrics are thin, you can join them with a single seam down to the very bottom. Mine are rather dense though and the transition from the zipper to the seam won't be neat enough in this case.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image040.jpg" alt=""></p>
<p><strong>I secure the seam allowances on the backing with pins to prevent them from getting in the way when I am sewing. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image042.jpg" alt=""></p>
<p><strong>And I sew the central back seam on the sequin pieces. </strong>I place the needle of the sewing machine right at the point where I stopped sewing in the zipper, make a very neat bar tack, and sew a short seam of about 10cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image047.jpg" alt=""></p>
<p><strong>I switch back to the regular presser foot (because the single-toe foot might not produce a seam of a good enough quality) and finish sewing the central back seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image049.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image051.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image053.jpg" alt=""></p>
<p><strong>Then I sew the central back seam on the backing pieces. </strong>&nbsp;I notch the seam allowance of the sequin fabric at an angle from two sides and align the bottom edges of the backing pieces. The sequin fabric is inside and does not get in the way. The central back seam on the backing does not have to stop right at the same point where the central seam starts on the sequin fabric. I leave a gap of about 1.5cm to avoid unnecessary tension and protect the garment from distortion.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image055.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image057.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image059.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image061.jpg" alt=""></p>
<p><strong>The resulting central back seam is very even and the transition to the zipper looks very neat.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image063.jpg" alt=""></p>
<p><strong>I will press open the seam allowances of the central back seam on the sequin fabric and the backing fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image065.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image067.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image069.jpg" alt=""></p>
<p><strong>I need to sew the side seams on the sequin overlay and the backing.</strong> I will first put the sequin pieces face to face and sew the side seams, then fold the assembled sequin face together in the middle of the lay, carefully even out the backing fabric and sew the side seams on it, and then press the side seams and turn out the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image071.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image073.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image075.jpg" alt=""></p>
<p><strong>The side seams on the sequin pieces are sewn with a 2cm seam allowance. </strong>Based on the results of the fitting, I have made the lining a little wider in the area from the waistline to the hip line by shifting the side seams at that level. Of course, I am supposed to do the same on the face of the dress. It is a very small adjustment though and, since the face fabric is elastic, I won't apply any changes. If we were talking about a more considerable change, I would have had to apply it to all face pieces when sewing them together.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image077.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2016_files/image079.jpg" alt=""></p>
