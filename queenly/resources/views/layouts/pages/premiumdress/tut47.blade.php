<p><strong>"Premium Dress"</strong> is a detailed guide to the sewing techniques employed in the making of a fantastic dress inspired by a famous Lebanese fashion designer - <strong>Krikor Jabotian</strong>.</p>
<p><strong>Peculiar features of this corset dress:</strong></p>
<ul>
<li>The pattern was purposely designed to keep the front and the back of the skirt whole instead of splitting them up by vertical princess seams.</li>
<li>An additional princess seam on the front half of the lining ensures maximum tight fit in the waist.</li>
<li>The neckline edge is treated in a new special technique.</li>
<li>The dress features concealed side seams.</li>
<li>The dress is made from three layers: lining, background fabric, and sequin overlay fabric.</li>
<li>There is a blind zipper and concealed inner lacing.</li>
<li>Ruffles are sewn on the see-through skirt in accordance with a complicated layout scheme.</li>
<li>There is a 3D decorative ruffle piece on the shoulder of the dress.</li>
<li>The bodice is draped.</li>
<li>The dress is complemented with a decorative buckle and matching hair clips in the form of plastic suede flowers.</li>
</ul>
<p><strong>This course will teach you:</strong></p>
<ul>
<li>How to customize the size of a corset dress pattern for your client.</li>
<li>How to cut sequin fabric with minimum princess seams and achieve a tight fit in the waist.</li>
<li>In what order to join the pieces to achieve the best results.</li>
<li>How to sew curved princess seams neatly on ï¿½complicated" fabric (sequin fabric).</li>
<li>How to treat necklines (a new technique).</li>
<li>How to sew a lining worthy of a premium class garment.</li>
<li>How to sew concealed lacing panels into the dress.</li>
<li>How to sew a skirt from delicate mesh fabric and decorate it with a generous amount of ruffles in two different colors.</li>
<li>How to cut ruffles from thin fabric.</li>
<li>How to reinforce see-through mesh fabric for sewing ruffles on it.</li>
<li>How to create additional accessories to complement the dress (a belt brooch and matching hair clips).</li>
<li>How to make and color flowers from plastic suede.</li>
<li>How to make your own flower and petal moulds for working with plastic suede.</li>
</ul>
<p><strong>You will find enclosed:</strong></p>
<ul>
<li>Ready pattern of a bell-shaped slightly flared dress with a train sewn into the central back seam.</li>
<li>Ready pattern of the top see-through skirt with a layout scheme for sewing on ruffles.</li>
<li>Flower templates for plastic suede accessories.</li>
<li>Swirl template for cutting decorative shoulder ruffles.</li>
<li>Corset pattern, pattern adjustment calculations table, and ruffle layout scheme.</li>
</ul>
<p><strong>The purpose of this course</strong> is to help you master all technical know-how required for sewing the most sophisticated designer dresses!</p>
<p><strong>The course will be helpful</strong> not only to beginners but also to those experienced in sewing. It is also suitable as a means of professional improvement for staff of sewing ateliers.</p>
<p>I would also like to emphasize that the dress from this course is merely a foundation for your own masterpieces. Let your creative spirit run free!</p>
