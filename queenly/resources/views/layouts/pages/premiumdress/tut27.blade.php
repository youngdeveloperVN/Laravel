<h1>Tutorial 26. Cutting Fabric Straps for the Ruffles. </h1>
<p></p>
<p><strong>Before we start preparing the fabric, I would like to give answers to the key questions that arise as soon as you see a skirt like ours</strong>:&nbsp; </p>
<p>- What gives the hem of the skirt such a beautiful wave-like shape?&nbsp; </p>
<p>- In what order do you sew ruffles onto the base of the skirt?&nbsp; </p>
<p>- How do you cut these ruffles?&nbsp; </p>
<p><strong>You already know the answers to the first two questions: </strong>&nbsp;the skirt has a wave-like hem because it is made of hard mesh fabric finished with a soft Rigilene boning tape (I might even add a Rigilene bone later); and the ruffles are sewn onto the base in accordance with the layout marked on the pattern.&nbsp; </p>
<p><strong>The answer to the third question is very simple: </strong>these wonderful ruffles are cut on the bias.&nbsp; </p>
<p><strong>I will cut the main fabric in straps strictly on the bias.</strong> The width of a ready strap will be 18cm. The straps will be rather short and I will gather them on a seam running through the middle. I will round the ends of each strap. After preparing all straps (basically turning them into ruffles), I will sew them onto the base of the skirt following the layout.&nbsp; </p>
<p><strong>Let's start cutting.</strong> My fabric has a width of 3m. I fold it in half two times to get a lay with a width of 75cm. Since each strap is supposed to be cut at an angle of 45 degrees, I mark a length of 75cm and draw a line to indicate the direction of the bias. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2026_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I have prepared a cardboard template with a width of 20cm for cutting the straps.</strong> It includes some extra space for heat sealing the edges with a soldering iron. <strong>I mark a couple of straps using the template...</strong></p>
<p>
