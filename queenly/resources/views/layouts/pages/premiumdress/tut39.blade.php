<h1>Tutorial 38. Analyzing the Ruffles Layout. The Center of the Back Half of the Skirt. </h1>
<p></p>
<p><strong>I need to finish the skirt by decorating the central part of its back. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2038_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I will sew ruffles onto either half of the red fragment and the green fragment (i.e. from either side of the skirt individually) and then sew the central back seam. </strong>It would be very hard to do it the other way round (first sew the central back seam and then sew ruffles onto whole fragments).</p>
<p><strong>Each half-fragment will start from a dark ruffle (point 1). Whenever the ruffle ends, I will put down a ruffle of the other color and continue doing so without paying attention to the color alternation lines.</strong> I believe such random color alternation will make the skirt even more beautiful. </p>
<p><strong>The last ruffle on each half-fragment stops at point 2. </strong>I leave its end detached. </p>
<p><strong>Only after doing all this, I sew the central seam. </strong>I step 20-25cm down from the waistline and join the two halves of the skirt down to the bottom keeping the seam allowance on the outside. I fold the seam allowance to the side on the hard mesh fabric, topstitch it, and trim it. And I leave it as is on the soft mesh fabric. </p>
<p><strong>After sewing the central back seam, I sew the detached ends of the ruffles onto the opposite halves of the skirt placing one above the other. </strong></p>
<p><strong>I also add a kind of "strengthening ribs" to prevent the central seam from sinking in under the weight of the skirt. </strong>I do this by sewing two additional ruffles in-between the ruffles at the top of the red fragment. Half of the ruffle (lengthwise) is secured on the left side of the skirt and the other half - on the right side of it. This secures the top part of the central back seam. The rest of the seam spreads out evenly under the weight of the skirt. The hard mesh fabric will secure the central seam additionally because of the seam allowance that has been folded to the side and topstitched.&nbsp; </p>
<p><strong>A colossal amount of work has been done and the skirt is nearly finished. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2038_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I still need to work on the hem though. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2038_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I urge you to think about the functionality of the dress.</strong> Since the dress is long, it will drag along the floor and the hem will eventually get dirty. We are making a premium dress though, i.e. an expensive dress which implies a top quality finish. <strong>I will use a chocolate-colored bias tape to finish the hem from the inside</strong>. It matches the color of the ruffles quite well.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2038_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Another thing I want to do is reinforce the hem and emphasize its wave-like shape. </strong>I will not only enhance the shape of the skirt but make it more comfortable, too. The wearer won't have to keep lifting the front of the skirt to walk. I will achieve this by sewing a wide Rigilene bone along the face side of the hem. It will help spread the skirt out down the bottom.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2038_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And, last but not least, I will sew ruffles on along the hem. </strong>I will sew them on in half-circles so as to repeat the shape of the fragments. Instead of gathering the prepared fabric straps in advance, I will do it in the sewing process so as to position the edge of the strap at an angle to the surface of the skirt and give it a half-circle shape. </p>

