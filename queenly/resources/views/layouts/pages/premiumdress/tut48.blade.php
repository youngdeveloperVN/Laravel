<p><strong>I have prepared all flower pieces.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2047_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>I also need some plastic suede fringe to complete the composition.</strong> I cut a strap of brown plastic suede with a width of 3cm and a length of about 15-17cm. I cut it into a fringe and color the edges with a yellow pastel pencil. I put the fringe to the iron sole, section after section, its colored side out. The fringe curls up. I will wrap it around one of the flower middles later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2047_files/image037.jpg" class="img-responsive center-block"></p>
<p>
