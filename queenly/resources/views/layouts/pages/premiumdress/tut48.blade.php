<p><strong>I start working on the small flower petals.</strong> I will put the petal in the mould face side down to make sure all veins get imprinted onto the face of the petal. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2047_files/image047.jpg" class="img-responsive center-block"></p>
<p><strong>I put the petal to the iron sole</strong> face side out and it curls up so that its face side becomes convex. While the petal is still hot, <strong>I quickly put it on the mould face side down </strong>and start "rubbing it in" to achieve a detailed imprint.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2047_files/image049.jpg" class="img-responsive center-block"></p>
<p>
