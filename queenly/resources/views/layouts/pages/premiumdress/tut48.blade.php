<p><strong>I start working on the larger parts of the flowers.</strong> I want to make one dormant bud, one blossoming bud, and one full bloom flower. For this purpose, I will not only press the first flower piece into the sponge with the larger ball-shaped knob but squeeze the sponge with my hands additionally. I will shape the second flower piece the traditional way and use the smaller ball-shaped knob for the third flower piece. In order to add some diversity to the flowers, I put the pieces of the first and the second flower with their back to the iron sole and the pieces of the third flower with their face to it. </p>
