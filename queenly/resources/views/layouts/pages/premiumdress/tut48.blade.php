<p><strong>Here is what the petals look like before and after this procedure: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2047_files/image053.jpg" class="img-responsive center-block"></p>
<p><strong>I do the same thing with all twelve small petals. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2047_files/image055.jpg" class="img-responsive center-block"></p>
<p><strong>WARNING:</strong> plastic suede shrinks when heated. You must account for it when designing your own templates. </p>
<p><strong>And now I need to work on the large petals.</strong> I want their face side to be convex.&nbsp; </p>
<p><strong>In order to achieve this, I take the petal face side down and stretch it widthwise from the middle to the edges giving it a convex shape.</strong> Plastic suede becomes flexible and acquires any required shape in warm hands and then turns solid again when cooled down.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2047_files/image057.jpg" class="img-responsive center-block"></p>
