<h1>Tutorial 47. Shaping the Petals.</h1>
<p></p>
<p><strong>Now I need to adjust the shape of the toned petals and flower middles.</strong> I have prepared an iron, a sponge, two knitting needles (one with a larger and another with a smaller ball-shaped knob), tweezers, three beads for the flower middles, a petal mould, and a crochet hook.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2047_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I will take advantage of the melting property of plastic suede that allows you to give it any desired shape which sets when the material cools down.</strong> </p>
<p><strong>I work on the shape of the inner parts of the flowers.</strong> I put the piece to the iron sole heated to the temperature normally used for silk and nylon. Please note that the edges of a plastic suede piece will always curl inwards when you put it to a heated iron sole. I put the face side of the smallest flower piece to the iron sole. There will be a bead in the middle of it. I put the heated piece on the sponge, press it down using the smaller ball-shaped knob of the first knitting needle, and wait for it to cool down. Here is a ready flower middle into which I will install a bead with the help of a glue gun.</p>
