<p><strong>The medium-size flower pieces should be put with their back (and not face) to the iron sole</strong>. The technique is the same as described above. Only this time I use the larger ball-shaped knob to press the piece down.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2047_files/image018.jpg" class="img-responsive center-block"></p>
