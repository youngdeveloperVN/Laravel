<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image014.jpg" alt=""></p>
<p><strong>The side front piece has fit onto two sheets.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image016.jpg" alt=""></p>
<p><strong>The seamless back piece takes up six sheets. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image020.jpg" alt=""></strong></p>
<p><strong>As I have said before, you need to sew a mock-up before altering the standard pattern. </strong></p>
<p><strong>I mark red points along all major circumference lines so I can align the pieces properly when I start joining them.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image022.jpg" alt=""></strong></p>
<p><strong>Notch the paper pieces down to the very seam allowance line before transferring them on fabric, and then transfer the notches and the alignment points as well.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image024.jpg" alt=""></p>
<p><strong>Remember to get the corresponding points aligned when sewing the pieces together.</strong> It will help you do it neatly and correctly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image032.jpg" alt=""></p>
<p><strong>Now I will start adjusting the standard pattern. </strong></p>
<p><strong>I start from the front side seam line. </strong></p>
<p><strong>I take the side front piece and shift the side seam line 1cm inwards in the area between the top line and the stomach line, and 0.5cm inwards at the hip line level. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image034.jpg" alt=""></strong></p>
<p><strong>I re-draw the front side seam line using the side front piece as a template:</strong> first I re-draw the section from the top line to the stomach line and then I turn the piece and bring the side seam line to the marked 0.5cm. <strong>Remember to re-draw the top line of the piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image038.jpg" alt=""></p>
<p><strong>I shorten <em>the new</em> side seam line and the intermediate princess seam by 1cm to adjust down the side length measurement. Please note</strong> that you can only do it after applying adjustments to the configuration of the side seam line! I mark a seam allowance of 2cm and re-draw the top line of the intermediate front piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image040.jpg" alt=""></p>
<p><strong>Next I need to alter the front princess seam line.</strong></p>
<p><strong>There were two possible options when I was calculating adjustments to the front princess seam line at the stomach level:</strong> I could either shift inwards only the intermediate princess seam line (the adjustment value would be -0.5cm) or I could also shift the bust princess seam line (and then the adjustment value would be -0.3cm).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image042.jpg" alt=""></p>
<p>Before taking the decision, I analyzed the situation to see how much this adjustment to the bust princess seam at the stomach level would affect the beauty of its shape and the alignment of the central front piece and the intermediate front piece.</p>
<p><strong>After re-drawing the bust princess seams, I saw that I could safely shift all front princess seam lines 0.3cm inwards at the stomach level.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image044.jpg" alt=""></p>
<p><strong>There are no other adjustments to the bust princess seams.</strong></p>
<p><strong>Next I need to adjust the intermediate front princess seam line.</strong> There are no changes at the bust line, the waistline, and the hip line. I shift it 0.5cm outwards at the under-bust level and 0.3cm inwards at the stomach level. <strong>I re-draw the intermediate princess seam line.</strong> Although it doesn't look that smooth, it will become a straight line when sewn.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image046.jpg" alt=""></p>
<p><strong>I have applied all adjustments to the side front piece.</strong> The new lines are marked red. I can cut the piece out and transfer it onto fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image048.jpg" alt=""></p>
<p><strong>I apply the same adjustments to the princess seam line of the intermediate front piece. I have already applied adjustments to the main bust princess seam line.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image050.jpg" alt=""></strong></p>
<p><strong>Now I need to adjust the length of the intermediate front piece.</strong></p>
<p><strong>I shorten the intermediate princess seam by 1cm and the bust princess seam by 2.5cm (according to the calculated value for adjusting the clip-toward-corner measurement). </strong>I re-draw the top line of the intermediate piece making sure to keep the clip toward the armscye at 10cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image052.jpg" alt=""></p>
<p><strong>I remove 2.5cm at the top of the central front piece by re-drawing the top line. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image054.jpg" alt=""></strong></p>
<p><strong>There is a peculiar thing about corset garments. </strong>On the pattern, the neckline does not look straight but somewhat lowered in the middle, by about 1cm. When you sew a mock-up though, you will see that the neckline is absolutely straight. It happens because of the cleavage area. When assembling the garment, I will add a bone in the middle which will push the neckline edge out and make it straight. I also account for a dart in the middle of the chest to remove excess fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image056.jpg" alt=""></p>
<p><strong>Considering that I have shifted the side seam 0.5cm inwards at the hip line level, I need to do the same on the central front piece now.&nbsp; </strong></p>
<p><strong>All new lines are marked red for your convenience.</strong> I only do it to make sure you can see them in the picture. You are supposed to cut along thin lines drawn with a pencil or a pen.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image058.jpg" alt=""></p>
<p><strong>I need to apply the last adjustment before cutting along the new princess seam line. </strong></p>
<p><strong>When I looked at my mock-up, I felt like setting the bust princess seams wider apart and making them straighter.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image060.jpg" alt=""></p>
<p><strong>It is easy to do this on the pattern: I simply shift the bust princess seam line 1.3cm to the side.</strong> Please note that I shift the princess seams against the <strong>initial</strong> top line of the central front piece and the intermediate front piece because my mock-up was made after the standard pattern. I re-draw the seam allowance lines (black dashed lines).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image062.jpg" alt=""></p>
<p><strong>At this moment, I can check how well the central front piece and the intermediate front piece join along the straightened bust princess seams, and examine the shape of the neckline.&nbsp; </strong></p>
<p><strong>I cut along the new princess seam line of the intermediate piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image064.jpg" alt=""></p>
<p><strong>I fold the seam allowance and align it with the new princess seam line of the central front piece. And I draw a graceful top line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image066.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%202_files/image068.jpg" alt=""></p>
<p><strong>This is all that concerns adjusting the standard pattern. Now I just need to cut out the front pieces following the new lines.</strong></p>

