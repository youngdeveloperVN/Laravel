<h1>Tutorial 4. Cutting the Veiling.</h1>
<p><strong>Before we start cutting the veiling, let me share a great trick that will help you save a lot of time. </strong></p>
<p>I cover the cutting table with a piece of fabric so I can draw guidelines right on it.</p>
<p><strong>I take a marker and draw two axes in the right corner at 90 degrees to each other.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image004.jpg" alt=""></p>
<p><strong>I need to cut strips of veiling with a width of 35cm for the first ruffle (1).</strong> I measure sections of 35cm from the marked vertical axis and draw vertical guidelines with a black marker. My cutting table fits four sections like that, which means I will be able to cut four strips of veiling at a time.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image006.jpg" alt=""></p>
<p><strong>I also need strips with a width of 55cm for the second ruffle (2).</strong> I take a blue marker and draw vertical guidelines at 55cm intervals starting from the same vertical axis. My cutting table fits two such sections, i.e. one layer of veiling will be enough for two strips.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image008.jpg" alt=""></p>
<p><strong>The third ruffle (3) will be made of strips with a width of 75cm. </strong>I take a green marker and draw new vertical guidelines at 75cm intervals. The cutting table fits two sections like this, i.e. I will be able to cut two strips at a time.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image010.jpg" alt=""></p>
<p><strong>The fourth ruffle (4) will be made of strips with a width of 90cm. </strong>I take a red marker and draw a single vertical guideline 90cm from the vertical axis. Only one section fits in.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image012.jpg" alt=""></p>
<p><strong>I have marked all guidelines! </strong></p>
<p><strong>I unroll the roll of veiling (it is usually folded two times) and align the fold line with the horizontal axis. I let the vertical edge of the veiling go just beyond the vertical axis and trim it neatly along it. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image014.jpg" alt=""></strong></p>
<p><strong>I start cutting 35cm wide strips for the first ruffle (1).</strong></p>
<p><strong>I cut the first four strips following the black vertical guidelines. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image016.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image019.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image020.jpg" alt=""></strong></p>
<p><strong>I put the ready strips of veiling aside. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image021.jpg" alt=""></strong></p>
<p><strong>I pull the veiling so as to align its vertical edge with the vertical axis again and cut the remaining three 35cm strips. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image023.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image025.jpg" alt=""></strong></p>
<p><strong>All seven strips for the first ruffle are ready! </strong>This time I am working with the smallest possible amount. If you are not limited in budget, then you can use up to ten strips of veiling for the first ruffle and increase the gathering ratio. The skirt will only look better!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image027.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image029.jpg" alt=""></p>
<p><strong>I start cutting 55cm wide strips for the second ruffle (2).</strong></p>
<p><strong>I adjust the veiling and align its edge with the vertical axis. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image031.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image032.jpg" alt=""></p>
<p><strong>I cut two strips following the blue vertical guidelines. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image034.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image035.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image037.jpg" alt=""></strong></p>
<p><strong>I adjust the veiling again and cut another two strips with the same width (55cm).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image039.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image041.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image042.jpg" alt=""></p>
<p><strong>I unroll the roll further, align the edge with the axis, trim it evenly, and cut the last two 55cm strips. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image043.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%204.files/image044.jpg" alt=""></p>
