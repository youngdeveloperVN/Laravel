<h1>Tutorial 2. Cutting the Base of the Skirt.</h1>
<p><strong>I start from cutting the lining of the skirt. </strong></p>
<p><strong>I mark the calculated radius (26cm) and the length of the lining (105cm) along the selvage of carefully pressed lining fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image004.jpg" alt=""></p>
<p><strong>I fold the lining fabric in correspondence with the bottom marked point aligning the selvages. </strong></p>
<p><strong>I fold the fabric in a triangle, aligning the selvages with the fold line, and then do the same one more time. </strong></p>
<p>It is a very convenient way to fold fabric for cutting full-sweep and half-circle skirts which allows you to cut them right on the ironing board.</p>
<p><strong>I mark the radius of the skirt (26cm) and the length of the lining (105cm) from the sharp point of the lay. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image018.jpg" alt=""></p>
<p><strong>And I cut a half-circle lining skirt. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image022.jpg" alt=""></p>
<p><strong>This is basically the whole cutting process. </strong>The remaining fabric can be used for sewing the bodice or for decoration purposes.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image024.jpg" alt=""></p>
<p><strong>You can either serge the hem of the skirt or heat-seal it with a pyrography tool or a soldering iron. I heat-seal it with a soldering iron using a cardboard template. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image026.jpg" alt=""></strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image028.jpg" alt=""></p>
<p><strong>The hem of the skirt is finished.</strong> You can decorate it with a lace trimming or do a baby-hem finish.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image030.jpg" alt=""></p>
<p><strong>Before finishing the top edge of the skirt, I need to adjust its shape in accordance with the V-shaped bottom edge of the bodice. </strong>I fold the skirt in half and mark a point 7cm from the top edge along the fold line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image032.jpg" alt=""></p>
<p><strong>I re-draw the top edge and cut along the new line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image036.jpg" alt=""></p>
<p><strong>The new shape of the top edge fully coincides with the shape of the bottom edge of the bodice. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image037.jpg" alt=""></p>
<p><strong>I heat-seal the top edge with a soldering iron. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image039.jpg" alt=""></p>
<p><strong>And I do the same with the central back seam edges. </strong>I even them out using a steel ruler.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image041.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%202.files/image043.jpg" alt=""></p>
<p><strong>The cutting process is not over yet. I need to draw all necessary guidelines to continue. </strong></p>

