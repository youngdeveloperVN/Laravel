<h1>Tutorial 7. Cutting and Sewing the Exterior Layers of Veiling.</h1>
<p><strong>I have successfully finished the lining of the skirt and now I can work on the face. </strong></p>
<p><strong>The face part of the skirt consists of three ruffles (5, 6, and 7), each with a width of 120cm, which are to be joined with the top edge of the lining. </strong>Each ruffle is made of three strips of veiling with a length of 300cm and a width of 120cm. These three strips need to be joined along the selvage in a single 9-meter long strip.</p>
<p><strong>But the bodice of my dress has a V-shaped bottom edge. I have already accounted for it on the lining and I must also account for it on these exterior layers of veiling. </strong>The sharp point must be naturally positioned in the middle of the 9-meter long strip of veiling. Thus before sewing together those 3-meter long strips, I will put three of them in a stack, fold them in half, measure 7cm down the fold line, and cut accordingly. The remaining six strips will stay as they are.</p>
<p><strong>I will position the strip with the V-shape in the middle when sewing three 3-meter long strips in a 9-meter long strip. Please be very careful when sewing these strips together. </strong></p>
<p><strong>It is also important to gather the 9-meter long strip of veiling correctly </strong>because the sharp point of the V-shaped edge must stay strictly in the middle of the gathered ruffle. I make a row of stitches along one half of the edge starting from the vertical edge and finishing at the sharp point. Then I make a bar tack, trim the thread, and make another row of stitches along the other half of the edge. I will then fold the piece in half, pin the middle to the cutting table or the dress-form, and start gathering the two halves gradually approaching the sharp point of the V-shape.</p>
<p><strong>I determine the length of the ruffle by measuring the bottom circumference of the bodice. It is 160cm in my case, which means each half needs to be gathered down to 80cm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image002.jpg" alt=""></strong></p>
<p><strong>One more thing: each ruffle must be gathered individually. </strong>In this case, the skirt will be fuller at the top (which is exactly what I am trying to achieve). If your skirt is not supposed to be that full at the top, you can gather all three ruffles on the same thread.</p>
<p><strong>Time to get back to work!</strong></p>
<p><strong>I put a stack of three 120cm-long strips of veiling on the table. They are folded in half. I measure 7cm down the fold line (distance from the waistline to the sharp point of the V-shaped edge), draw a horizontal guideline, and trim the top horizontal edge of the strip in an arc shape. </strong>By doing so, I have accounted for the V-shape on all three face layers of veiling.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image006.jpg" alt=""></p>
<p><strong>I start assembling the three 9-meter long strips. Each strip is made of three 3-meter long strips. The strip with a V-shaped edge must be positioned in the middle. </strong></p>
<p><strong>I start making a row of stitches along the first half of the strip to gather it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image008.jpg" alt=""></p>
<p><strong>I make a bar tack as I reach the sharp point of the V-shape and cut the thread. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image010.jpg" alt=""></p>
<p><strong>I make a new bar tack and start a new row along the other half of the strip from the sharp point of the V-shape to the opposite vertical edge of the strip. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image014.jpg" alt=""></p>
<p><strong>I carefully gather each half of the strip down to 80cm from the centre of the V-shape to the vertical edges. </strong></p>
<p><strong>I put together the resulting ruffles (5, 6, and 7) directing all seam allowances to the same side and pin them together along the top edge starting from the centre of the V-shape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image024.jpg" alt=""></p>
<p><strong>Before sewing the ruffles together along the top edge, I pin them together along the vertical edges in the zipper area which goes about 25cm down from the top edge. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image026.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image028.jpg" alt=""></strong></p>
<p><strong>I mark a point 25cm down from the top edge and insert the last pin a little below it to prevent the layers from coming apart during the sewing. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image030.jpg" alt=""></strong></p>
<p><strong>And I do the same thing with the other vertical edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image032.jpg" alt=""></p>
<p><strong>I start joining the ruffles along the vertical edges moving up from the bottom point of the zipper. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image034.jpg" alt=""></p>
