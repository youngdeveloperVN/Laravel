<p><strong>And then I join the ruffles along the top edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%207.files/image040.jpg" alt=""></p>
