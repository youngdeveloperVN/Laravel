<h1>Tutorial 6. Finishing the Lining of the Skirt.</h1>
<p><strong>Below is a picture of the lining with all four ruffles on it. </strong>The ruffles disguise the bottom edge of the lining and the petticoat hoops. The bottom edge, the hoops, and the joining seams will be absolutely invisible after I put the additional three ruffles on the skirt! Even at this early stage, the shape of the skirt looks correct and its bottom diameter is about 2m.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image002.jpg" alt=""></p>
<p><strong>I will finish the lining completely in this tutorial. </strong></p>
<p><strong>It will be done in the following order:</strong></p>
<p><strong>- I will join the vertical edges of the ruffles. </strong>Remember how I left 2cm seam allowances from either end? Now I will fold the skirt in half, face to face to keep the veiling inside, and carefully sew together the vertical edges of all four ruffles starting from the top one. The seam allowance will not exceed 5mm and it will face the lining side.</p>
<p><strong>- Without turning out the skirt, I will sew the central back seam leaving 20-25cm at the top for installing a zipper.</strong> I will leave a small slit in the central back seam of the lining instead of sewing the zipper in it. The zipper itself will be sewn in the face layer of ruffles which will overlay and conceal the slit in the lining. This will help you spare a fair bit of time.</p>
<p><strong>- I will make a row of stitches along the top edge of the skirt </strong>to prevent it from getting stretched out and to prepare for sewing the skirt to the bodice.</p>
<p><strong>I start joining the vertical edges of the ruffles. I start from the fourth ruffle with a width of 90cm (4). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image004.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image006.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image008.jpg" alt=""></strong></p>
<p><strong>I never make bar tacks when sewing with veiling because it may get drawn together. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image009.jpg" alt=""></strong></p>
<p><strong>I join the vertical edges of the remaining ruffles (3, 2, and 1). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image011.jpg" alt=""></p>
<p><strong>After sewing together the vertical edges of all four ruffles, I can sew the central back seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image013.jpg" alt=""></p>
<p><strong>I sew from down to up carefully aligning the vertical edges of the lining. </strong>If there happens to be a small mismatch in their lengths, I will even it out at the top because the bottom edge has already been heat-sealed. I stop 20-25cm from the top edge of the skirt, make a bar tack, and finish the stitching. The seam allowance is 1.2cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image017.jpg" alt=""></p>
<p><strong>I have joined the vertical edges of all ruffles and sewn the central back seam of the lining. </strong>When I was putting the ruffles on the lining, I always left a 2-3cm allowance from either end of the ruffle so I could later sew these edges together. <strong>And now I will sew these parts of the ruffles to the lining with short horizontal rows of stitches. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image019.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image021.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image023.jpg" alt=""></strong></p>
<p><strong>The next ruffle:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image025.jpg" alt=""></strong></p>
<p><strong>I reinforce the top edge of the lining with a row of stitches made 1.2cm from it. </strong>I start and finish sewing at a distance equal to the seam allowance of the central back seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image027.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image029.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image031.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image032.jpg" alt=""></p>
<p><strong>The lining is now finished. All I need to do is press open the seam allowances of the ruffles and the central back seam. I will also fold the seam allowances in the zipper area and press them flat. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%206.files/image034.jpg" alt=""></p>

