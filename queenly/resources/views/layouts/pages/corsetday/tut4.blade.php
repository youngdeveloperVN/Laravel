<h2>Tutorial 4. Finishing the Corset.</h2>
<p><strong>I continue joining the face part with the lining of the corset. </strong></p>
<p>I switch to a one-sided presser foot and make connecting stitches from the side of the lining. I make bar tacks at the intersection point of the strengthening stitches and not along the edge of the garment.</p>
<p><strong>I start sewing together the pieces along the vertical cut edge of the back: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image002.jpg" alt=""></p>
<p><strong>Now I am joining them along the top of the corset: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image003.jpg" alt=""></p>
<p>And finally I finish the process by joining the pieces along the vertical cut edge of the second half of the back.</p>
<p>I remove the pins and check how well the seam is made. If I happen to notice that a pin has torn the thread somewhere I always re-stitch this seam for the second time.</p>
<p><strong>I trim the seam allowances leaving a 0.2 inch gap and trim the corners on a slant: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image005.jpg" alt=""></p>
<p><strong>Now I turn the corset inside out. </strong></p>
<p>I have placed a horizontal Rigilene bone going along the top from the underarm area to the middle of the back, that�s why there is no need for a clean-finish-edge seam to be made when turning the garment inside out. You can spread the corners with the help of a sack needle.</p>
<p><strong>After the corset has been turned inside out it needs to be pressed. </strong></p>
<p>I am pressing the spot where the lacing is meant to be. I am making a pretty piped-like edge that is literally 1 mm thick thanks to the boning. I am thoroughly spreading the top of the corset along the bone going from the back to the underarm. Then I take the cushion and the cup pressing mould again and place the corset cup over it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image006.jpg" alt=""></p>
<p>I am trying to form a pretty cutting line by pressing it very thoroughly. Let me repeat that a corset like this is going to keep its shape only relatively. Of course this is a soft and simple corset and yet it is going to improve the body shapes and be comfortable to wear. There is no excessive use of boning in this corset that could obstruct your movements or squeeze too tight. And you cannot walk with a stoop wearing a corset like this. Its owner will have to keep her back straight whether she likes it or not.</p>
<p>Now I am pressing the neckline cut. There was a slight easing-in underneath the organza and now I press it away, straighten it and make the cut and the cup look rounded. A good fitting of the neckline cut is achieved by the degree of fullness along the cutting line where it is rounded. The smaller the bust is - the more fullness we need, the larger the bust is - the less fullness is required. I am using the moisture removing pad to set the pressed shape.</p>
<p><strong>My corset already holds its shape even after using such a simple technique: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image008.jpg" alt=""></strong></p>
<p>And again I put a decorative thread onto the spindle and stitch along the lacing line, along the Rigilene bones, looking to the previous stitches attaching these bones. This way I am making tunnels into which plastic bones will later be inserted.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image010.jpg" alt=""></p>
<p>I do the same thing with the second half of the back. I make bar tacks at the beginning and the end of these stitches despite the fact that they can then be seen from the face side. The threads will get undone if you don�t use any bar tacks here.</p>
<p><strong>I am measuring the supporting plastic bones: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image012.jpg" alt=""></p>
<p>For this purpose I am placing a plastic bone over the tunnel and mark the required length. There should be some 0.6 inch space left down the bottom. The tunnels have the same length so I can cut 2 of them straight away. I round the edges of the plastic bones and push them into the tunnels. Now we have two rounded curves. A lacing placket designed this way won�t be deformed even after extensive tension - it will stay even and straight on your back.</p>
<p>I measure the length of plastic bones for each curve consequently leaving a 0.6 inch gap down the bottom. I always measure two bones at a time to insert them both into the right and the left part of the corset.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image013.jpg" alt=""></p>
<p>I use the stitched-on Rigilene bone as a tunnel on the curves. I push a plastic bone under the wide Rigilene one which had been stitched-on to the curve.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image014.jpg" alt=""></p>
<p>This plastic bone forms an embossment on the face side which looks very decorative.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image015.jpg" alt=""></p>
<p>I am inserting the prepared plastic bone into the symmetrical back piece from the second side.</p>
<p>This should be repeated for each curve line.</p>
<p>I have inserted only one plastic bone into the side seam although two parallel Rigilene bones have been adjusted. You may do as you wish and insert two of them for additional decoration.</p>
<p><strong>This is what the corset looks like when plastic bones have been inserted: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image017.jpg" alt=""></p>
<p><strong>I have approached the final stage - now I need to join the corset along the bottom edge. </strong></p>
<p>I pin together the pieces of the face and the lining along the bottom.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image018.jpg" alt=""></p>
<p>Here you have to pin along the Rigilene bone which has been stitched-on along the bottom of the lining. I am joining all curves and all cut edges. It is done the same way as with the top of the garment: I pin at the controlling points and then add more pins in-between. This way the fabric is arranged most evenly.</p>
<p><strong>This is what it looks like face-on: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image020.jpg" alt=""></p>
<p>Even without any particular fusing the corset holds its shape thanks to its good pattern.</p>
<p>Now I return to the sewing machine. I measure the vertical length of the back and make some marks with a disappearing ink pen from both sides of the back where the lacing should be so that the length is be the same. I place the lower cut edge under the machine and make a stitch over the lining side leaving a presser foot long gap from the bottom bone. I start at the marked point and finish at the point marked with the pen. Now the pins should be removed.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image022.jpg" alt=""></p>
<p>I take a 0.8 inch bias tape, place it at the face side of the corset over the joining seam and stitch close to the edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image024.jpg" alt=""></p>
<p>Thanks to the joining seam I can see the direction and the spot where the tape should be. The stitch lies at a presser foot distance from the bone which lets the machine sew easily and comfortably. I trim the edge of the tape leaving 0.8-1 inch space.</p>
<p>I fold the tape under, secure the edge and pin it.</p>
<p>Afterwards I sew-on the tape by hand with a blind stitch.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image026.jpg" alt=""></p>
<p><strong>I finish pressing the bottom of the corset:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image028.jpg" alt=""></p>
<p><strong>Now I�m sewing a flap pieces of fabric that will be placed under the lacing: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image030.jpg" alt=""></p>
<p>It is a rectangle that is stitched from three sides, then turned inside out and stitched along the fourth side with a bar tack. I�m sewing this flap piece to the lining by hand with blind stitches or using a machine, right through the corset.</p>
<p>Now I�m punching through the grommets. The optimal distance between the grommets should be about 1.2-1.4 inches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image031.jpg" alt=""></p>
<p><strong>And here we go, we have this pretty corset: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/4_files/image032.jpg" alt=""></p>
<p>This corset is very comfy and light. It suits young girls, can be matched with various accessories and worn either with jeans or a skirt. A corset like this could also be suitable for wearing to prom.</p>
<p>This is all for now, you were learning with Tatiana Kozorovitsky - until the next time!</p>

