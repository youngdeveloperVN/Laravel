<h3>Tutorial 17. Finishing the Short-Sleeve Lace Bolero Piece.</h3>
<p></p>
<p><strong>I finish the straps or the bolero piece with a pretty frill elastic band using the zigzag stitch. </strong>I sew the elastic on along the entire perimeter of the piece and the sleeves. It's a very basic way of finishing the edges: the only challenge is not to confuse the left side with the right one. In order to avoid that, I lay the pieces down in a vertically mirrored fashion, top to top. Donï¿½t pull the elastic band too much. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>After finishing the open edge at the front I move to the neck hole and make a button loop at the top. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And then I sew the same kind of an elastic band onto the other half of the bolero.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>From the sleeve side of the piece the elastic should only go along the sleeve itself.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the elastic onto the face side and secure it with zigzag stitches.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Look how lovely it looks!</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>The edge of the front looks like this when finished:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Next I finish the sleeves.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I sew down the side seam.</strong> You can use an overlock machine, a flat lock machine or zigzag stitches. I do the latter. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>The bolero piece looks really delicate and beautiful.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>Only the top edge still needs to be finished.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image048.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I will fold the top part of the bolero and sew a hook or a button onto it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>And I will also fold the bottom edge of the bolero and sew it to the dress by hand or by machine. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>It is pretty much a lace shoulder wrap. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same steps with the second half of the bolero. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2017_files/image058.jpg" class="img-responsive center-block"></strong></p>

