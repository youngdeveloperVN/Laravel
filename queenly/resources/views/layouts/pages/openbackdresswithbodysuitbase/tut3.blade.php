<h3>Tutorial 3. Working on the Dress Pattern.</h3>
<p></p>
<p><strong>We are about to start working with the patterns.</strong> I have printed all of them out. There are separate sheets for the bodysuit, the dress and the shoulder piece made of lace. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>This DVD includes several versions of shoulder piece patterns:</strong> </p>
<p>- With dropped shoulder sleeves; </p>
<p>- With set-in sleeves (what is more, you can equally use this pattern with or without the sleeves). The armscye is located in its natural place allowing you to make a blouse or a sleeveless top alike. </p>
<p>- Halter-neck version. </p>
<p><strong>I have decided to choose the dropped shoulder version for our dress. </strong>&nbsp;</p>
<p><strong>Now we can start preparing the patterns.</strong></p>
<p><strong>Make sure to select 'actual size' in your printing preferences before you print the patterns out. </strong></p>
<p><strong>Once you have printed the patterns out, align them and glue them together. </strong>You will find a separate file titled 'How to Align Your Pattern Sheets.'&nbsp; Besides, there is a thumbnail on each pattern sheet with the relevant part of the pattern marked grey. All sheets are numbered. I tried to make the assemblage process as simple as possible. </p>
<p><strong>Attention!</strong> Printers tend to leave unprinted borders on the sheets. Do not trim them - just glue the sheets together as is. The grid on the sheets is meant to merge vertically and should align perfectly! Simply take a glue-stick and join the pieces.</p>
<p><strong>The lines inside the patterns mark their seam allowances.</strong> Most of them are one inch (or 1.2cm), except for the seam allowance of the central seam at the back where you insert the zipper (2cm). Should there be any variation in the seam allowances, you will see it on the pattern.</p>
<p><strong>I glue together the pattern of the back piece without trimming the edges. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same for the front piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Each time you are making a dress with ready-made cups that differ from mine, you will need to adjust the cradle on the pattern. It all depends on the style of the cups, their shape and placement.</strong></p>
<p><strong>Let me demonstrate how you do it.</strong> It is no coincidence that I've drawn a straight line along the under-bust line. We will use it for further reference. </p>
<p><strong>I fold the pattern along the under-bust line. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I put it down on a blank sheet of paper to re-draw the cradle. </strong>I mark the edge of the pattern and prolong it upwards in a straight line. The distance from the centre (or the length of the bridge between the cups) is chosen based on the size, style and configuration of the cups. Usually, it is 1-1.5cm. <strong>I mark 0.7cm from the edge of the cup and draw a line as shown below. The bridge between the cups on my garment will be 1.5cm.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Needless to say, you should buy the cups and matching semi-circle underwires in advance.</strong> We need to determine, most likely directly on the dress-form, at what incline the cup should be placed because <strong>you could either place it with both corners at the same level or slightly turn it about.</strong> It all depends on your taste and design concept!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>It is just as relevant for round cups.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Now that I've made up my mind about the location of the cup, I put it down and mark the corners.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Please note</strong> that cup underwires have different ends: the rounder end is supposed to face the centre of the garment while the opposite end should face the underarm area. You must always account for it in pattern construction.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image020.jpg" class="img-responsive center-block"></p>
<p>The underwire will give the cup its final shape. It will happen after you sew tubular bone casing onto the cup and insert the underwire into the tunnel. <strong>Thatï¿½s why the underwire - and not the cup - is of primary importance to our pattern.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p>The length of my underwire perfectly matches the bottom edge of the cup. <strong>I place the bone with one end at the determined point making sure it corresponds with the bottom border of the cup marked underneath the bust. I outline the underwire. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Now I know what the cradle looks like. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let's add a seam allowance to it.</strong> If the cups are sewn <strong>on</strong> the way you normally do it with see-through corsets, then you can use a 1.2cm seam allowance. However if they are sewn <strong>in</strong> according to the traditional approach, seam allowance to seam allowance, then I suggest you use 0.7-0.8cm to make things easier. Or you can mark the 1.2cm seam allowance first and trim it later.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I finish drawing the side edge using the standard pattern as a template. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>And I draw the top edge.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong>A correctly chosen underwire is meant to be a little shorter than the bottom arch of the cup. Follow the contour of the cup to finish drawing the cradle. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>See how easy it was to adjust the cradle after a new cup style?</strong> The algorithm is the same regardless of the way you place the cups. The key is to place the underwire correctly, outline it, align it with the cup, mark it, and connect the points. <strong>In other words, you can use any kind of cups and not necessarily the same as mine! Just build your own cradle on the pattern.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to apply the alterations calculated for the particular client. </strong></p>
<p><strong>The side seam contour of the front piece:</strong></p>
<p><strong>I shift it 1.5cm inwards at the bust-line level, 2cm at the under-bust level, 1cm at the waistline level, 0.5cm at the stomach level 12cm below the waistline, and 0.5cm at the hip level.&nbsp; </strong>It's important to check the length of the garment with regard to the style and your client's height. <strong>I also need to shift the side seam down by 2cm. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image038.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I re-draw the side seam contour of the front piece connecting the reference points by hand or using the pattern as a stencil.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%203_files/image040.jpg" class="img-responsive center-block"></p>

