<h1>The Button Loop Closure.</h1>
<h2>Part 2.</h2>
<p><strong>Now you know the common technique. </strong></p>
<p><strong>There were some other questions in your E-mails though, and so I have decided to demonstrate the process of making a particular type of a loop closure with small closely-spaced buttons. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image002.jpg" alt=""></strong></p>
<p><strong>Let us go through certain commonly made mistakes for a start. </strong></p>
<p><strong>Please take a look at the shape of the loop made in the technique I demonstrated above. </strong></p>
<p><strong>The loop is round and it has a bottom. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image004.jpg" alt=""></p>
<p><strong>It is a common mistake when you make basically straight loops without any pronounced bottom. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image006.jpg" alt=""></p>
<p><strong>What happens in this case? </strong>When there is little distance between the legs of the loop, then it has to be quite long for the button to pass through. This results in a very large gap between the edges of the closure, which simply does not look good!</p>
<p><strong>For this reason, you must compare the button diameter with the loop length when you start marking your loops on squared paper. </strong>Try to make the loop as short as possible. This will allow you to achieve the best results!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image008.jpg" alt=""></p>
<p><strong>The offered method of making the loop closure is perfect when you want to have some space between the buttons. </strong></p>
<p><strong>But what to do if you want to use very small buttons positioned edge-to-edge? I will explain. </strong></p>
<p><strong>As I said before, the first thing you must do is reinforce the closure area. </strong></p>
<p><strong>I used an organza ribbon for that purpose in the previous example, and this time I will use a lace ribbon. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image010.jpg" alt=""></strong></p>
<p>I am working with very thin delicate stretch mesh fabric and very thin lace ribbon which stretches a little bit, too. Who knows how the sewing machine will behave in this case? How will the feed dogs behave?</p>
<p><strong>In order not to risk it, I take a piece of paper (plain or squared), put it under the mesh fabric, and put my lace ribbon on top of the lay. </strong>I secure the lay with pins so as to be able to see the central vertical line for cutting the fabric in the two halves of the back and the supposed guidelines for strengthening stitches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image012.jpg" alt=""></p>
<p><strong>I make two rows of strengthening stitches in the same direction. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image014.jpg" alt=""></strong></p>
<p><strong>Then I remove the paper. </strong>It is best to fold it over the row of stitches and then tear it away along the resulting perforated line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image018.jpg" alt=""></p>
<p><strong>The closure area is now reinforced. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image020.jpg" alt=""></p>
<p><strong>I can cut the piece in two halves. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image022.jpg" alt=""></p>
<p><strong>Next I only need to finish the edges by folding and then pressing them. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image024.jpg" alt=""></strong></p>
<p><strong>After finishing the edges of the closure, I mark the positions of all buttons and sew them in place. </strong>Marking button positions simply means marking points at intervals equal to the button diameter. As the result, all buttons form a single row without gaps.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image026.jpg" alt=""></p>
<p><strong>And now please let me put this work aside for a moment to answer a very important question from your E-mails: how to prevent thin mesh fabric from getting stretched out along neckline, armscyes, or along loop closure edges at the back of a garment? </strong></p>
<p><strong>Most of you must have heard of tambour embroidery. </strong>I explained how to work with the luneville hook in my free tutorials, but there are other types of hooks used for tambour embroidery. These hooks have a free-moving latch.</p>
<p><strong>There is a Japanese latch hook (right) and a latch hook made by Clover (left) in the picture below. </strong>I use these two hooks to prevent thin mesh fabric from getting stretched out.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image028.jpg" alt=""></p>
<p><strong>Before getting down to work, you need to mount in the embroidery hoop that part of mesh fabric which you want to prevent from getting stretched. </strong>By mounting it in the embroidery hoop I mean simply securing part of the mesh fabric in the hoop without tightening it the way you do for embroidery.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image034.jpg" alt=""></p>
<p><strong>I will use the Clover latch hook to make a row of chain stitches along the edge of mesh fabric which needs to be protected against stretching.</strong></p>
<p><strong>I will work with rather thick thread for better demonstration. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image036.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image038.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image040.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image042.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image044.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image046.jpg" alt=""></strong></p>
<p><strong>This is how you make chain stitches with the Clover latch hook. </strong>You can use thick thread on a ready garment because you will need to undo this row of chain stitches anyways after joining the pieces. You only need it temporarily to reinforce the edge of the mesh fabric piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image052.jpg" alt=""></p>
<p><strong>I believe you got the point. Do not tighten the last stitch so you can undo the full row later. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image054.jpg" alt=""></strong></p>
<p><strong>The piece will not get stretched in work this way. The armscye, for example, will preserve its original shape. </strong></p>
<p>After reinforcing the edge, you can finish cutting the piece and even carry out a fitting.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image056.jpg" alt=""></p>
<p><strong>Let us continue working on the loop closure. </strong></p>
<p><strong>We have two pieces: one has closely-spaced buttons along the edge and the other is supposed to have matching loops. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image058.jpg" alt=""></strong></p>
<p><strong>I start marking loops in correspondence with the button diameter. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image060.jpg" alt=""></strong></p>
<p><strong>Then I take thick thread and fold it right in half. </strong>The folded thread must be much longer than the loop closure.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image062.jpg" alt=""></p>
<p><strong>I secure the folded end of the thick thread in the top point of the guideline using a threaded needle. </strong>Please note that I only secure the thick thread and not sew it down completely. It should be able to move.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image064.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image066.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image068.jpg" alt=""></p>
<p><strong>Next, I keep arranging the folded thick thread along the closure edge and securing it with hand-made stitches in the marked loop points. </strong>The stitches are supposed to wrap around the thick thread. After securing the thread in the marked point, I pull the rest of it to the next point and do the same.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image070.jpg" alt=""></p>
<p><strong>I reach the end of the closure and secure the thick thread with hand stitches section after section. </strong>The loop thread must be able to move freely so you can tighten the loops. I leave a tail at the end.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image072.jpg" alt=""></p>
<p><strong>And I start fastening the buttons.</strong></p>
<p><strong>The key advantage of these loops </strong>is that when you stretch out one loop to pass a button through, the adjoining loop automatically gets tightened.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image074.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image076.jpg" alt=""></p>
<p><strong>After fastening about a half of all buttons, I noticed that the closure was getting distorted. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image078.jpg" alt=""></p>
<p><strong>This was why I left that tail at the end of the loop thread ï¿½ it will allow me to even out the loops. </strong></p>
<p><strong>I unfasten the buttons, go back to the beginning of the closure, and start expanding the loops one by one. </strong>In other words, I make them a little looser to account for the thickness of the button shank and avoid excessive tension and distortion.</p>
<p><strong>I must warn you that this method of making the loop closure only works with small closely-spaced buttons which resemble a string of beads! </strong>The loops will become visible as soon as there are some gaps between the buttons, and it will not look very good.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image080.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image082.jpg" alt=""></p>
<p><strong>I re-fasten the buttons after loosening the loops. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image084.jpg" alt=""></strong></p>
<p><strong>I spread out the closure.</strong> With the closure spread out properly, you should secure the loop thread tail to prevent it from popping out by sewing it firmly to the base of the closure.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image086.jpg" alt=""></p>
<p><strong>We have coped with the task, the loop closure is ready! </strong>The loop length is correct and the closure looks neat and pretty. The buttons form an even row and resemble a string of beads!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image088.jpg" alt=""></p>
<p>I have tried my best to answer your questions in this small tutorial, explain the common technique of making the loop closure, and give detailed guidelines to making a particular type of a loop closure suitable for small closely-spaced buttons imitating a string of beads at the back of the dress.</p>
<p>In addition, I have shown you how to secure the supposed edges of soft elastic mesh fabric with chain stitches before actually cutting it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/loops/tutorial%202.files/image090.jpg" alt=""></p>
<p><strong>I hope you will apply your new skills widely in your further sewing projects! </strong></p>

