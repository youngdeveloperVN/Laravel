<h1>Tutorial 15. Advancing to the Top of the Skirt.</h1>
<h2>Applying Necessary Adjustments.</h2>
<p><strong>I have made three rows of 7.5cm scales along the back and along the front of the skirt. There are 2cm intervals between them. </strong></p>
<p><strong>I have spent 140 scales and put 18 more aside to decorate the side seams later. </strong></p>
<p>Scales with a diameter of 7.5cm are not so different from those with a diameter of 8cm so three rows is more than enough.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image004.jpg" alt=""></p>
<p><strong>Next, I make six rows of 7cm scales on the back and as many on the front. The intervals are 2cm. </strong></p>
<p><strong>It has taken 290 scales and I put 15 more aside for decorating the side seams later. </strong></p>
<p>Although the diameter of these scales differs from that of the previous ones by just 1cm, it is already noticeable on the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image008.jpg" alt=""></p>
<p><strong>I had warned you that you might get faced with various errors when putting decorative elements on the garment, and such errors might need to get fixed.</strong></p>
<p><strong>And now I have noticed that the arcs on the back gores have come out deeper than needed. </strong></p>
<p>This probably happened because the intervals between the rows were a little larger than 2cm at the princess seams.</p>
<p>This flaw must be fixed without delay to prevent distortion of the overall pattern.</p>
<p><strong>There is a quite easy way to fix it. </strong>The subsequent rows are supposed to be made of 6.5cm scales. I will start from making a half-arc row on each gore of the back 2cm from the previous row along the centre lines of the gore.</p>
<p>Then, I will make a full row 2cm from the previous one.</p>
<p>And then I will make another identical half-arc and thereby decrease the depth by 4cm, which is enough.</p>
<p>The next rows will be full and positioned 2cm from one another.</p>
<p><strong>Because of situations like this, I highly recommend you should examine the results at intermediate stages of work so you can make adjustments promptly if the need comes. </strong></p>
<p><strong>You must always keep the process in control! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image010.jpg" alt=""></p>
<p><strong>After adjusting the back half of the skirt, I make five rows of 6.5cm scales at 2cm intervals on the front and on the back. </strong></p>
<p><strong>It has taken 230 scales and I have put 10 more aside for decorating the side seams. </strong></p>
<p>The picture below shows that I have almost finished the flared area. It will take just a couple more rows to pass onto the straight part of the skirt.</p>
<p>And you can clearly see the difference in the size of the scales now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image014.jpg" alt=""></p>
<p><strong>I use 6cm scales for the next five rows. These rows will be made at 1.5cm intervals. </strong></p>
<p><strong>It has taken 200 scales for these five rows of 6cm scales and I have put 12 aside for the side seams. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image016.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image018.jpg" alt=""></strong></p>
<p>The flared part of the skirt is now fully decorated, and I only need to work on the straight-fit top part which is about 40cm long.</p>
<p>I like how the shape of the skirt is coming out so far. There is a really smooth transition from one size of scales to another. I have measured the side seam edges and they are all even, too.</p>
<p><strong>I made a small change when putting 6cm scales on the skirt. </strong>You probably remember that the scales overlapped at the princess seams forming corners which I would then overlay by horizontally positioned scales. This was done in order to prevent the scales from parting and revealing the backing fabric when the gores fold in the flared area. Those horizontally positioned scales prevented this from happening although they did slightly break the overall pattern.</p>
<p>Now I am working on a basically straight area and there is no more need in putting those scales over the princess seams. The beauty of the pattern is more important here. And this means I will no longer put horizontally positioned scales over the overlapped scales at the princess seams.</p>
<p><strong>Upon examining the created pattern and texture, I can say that it has come out really smooth. </strong>It looks very good on the flared part of the skirt, but I feel like accentuating the princess seams on the straight part of it, especially at the front of the skirt. I want the princess seams to be more pronounced.</p>
<p><strong>And this effect is not hard to achieve! </strong>I only need to deepen the arc-shaped rows of scales on the gores of the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2015.files/image020.jpg" alt=""></p>
<p><strong>Here is what I will do: </strong>when I start working with 5cm scales, I will position the first four rows at 2cm and not 1.5cm intervals along the princess seams and the side seam edges and keep it at 1.5cm along the centre lines of the gores.</p>
<p><strong>By doing so, I will make each new arc-shaped row 5mm deeper than the previous. Therefore, four rows positioned this way will deepen the gores by a total of 2cm. </strong></p>
<p>This is more than enough to highlight the princess seams. Those corners formed by overlapped scales will make them stand out against the background of the overall pattern.</p>
<p>After bringing the rows to the desired depth with the help of these four rows of scales, I will position the rest of the rows at 1.5cm intervals along the princess seams and the side seam edges, as I had intended from the start.</p>
<p><strong>And now I suggest we should mark the new guidelines on the skirt and put a couple of rows on the skirt together. </strong></p>

