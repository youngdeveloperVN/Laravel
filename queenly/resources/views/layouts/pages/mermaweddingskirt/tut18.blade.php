<h1>Tutorial 17. Putting Decorative Scales in the Zipper Area.</h1>
<p><strong>I start adjusting the layout in the zipper area. </strong></p>
<p><strong>The central half-arc below the bottom end of the zipper will be covered by three rows of scales arranged chessboard fashion. </strong></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image002.jpg" alt="" width="617" height="341"></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image004.jpg" alt="" width="617" height="195"></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image005.jpg" alt="" width="617" height="223"></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image007.jpg" alt="" width="617" height="322"></p>
<p><strong>I will position the last scale horizontally aligning its straight edge with the bottom end of the zipper.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image008.jpg" alt="" width="617" height="257"></strong></p>
<p><strong>The final result will look like this: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image010.jpg" alt="" width="617" height="242"></strong></p>
<p><strong>After decorating the central part, I will continue forming rows of scales along the two side half-arcs situated between the side seam edge and the zipper. These rows will be positioned with 1.5cm intervals. </strong>In this case, the zipper will get overlaid by the scales.</p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image011.jpg" alt="" width="617" height="251"></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image012.jpg" alt="" width="617" height="238"></p>
<p><strong>This is the adjusted layout for putting scales in the zipper area of the skirt. </strong></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image014.jpg" alt="" width="617" height="324"></p>
<p><strong>I am still to decide what to do with the symmetric area at the front of the skirt. But let us finish the back first.&nbsp; </strong></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image015.jpg" alt="" width="613" height="396"></p>
<p><strong>I have overlaid the central corner at the back of the skirt with 4.5cm scales before the beginning of the zipper and started the first long row from side seam edge. </strong></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image017.jpg" alt="" width="619" height="416"></p>
<p><strong>The key thing is to put the last scale as close as possible to the zipper without catching its teeth. </strong>If you catch the teeth, you won�t be able to undo the zipper.</p>
<p><strong>The technique is the same as before: </strong>I secure a scale, press the reverse stitch button, put down the next scale, etc.</p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image018.jpg" alt="" width="608" height="393"></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image019.jpg" alt="" width="617" height="388"></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image021.jpg" alt="" width="617" height="291"></p>
<p><strong>I put the next scale from the other side of the zipper, as close as possible to it, </strong>and continue sewing until I reach the opposite side seam edge.</p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image023.jpg" alt="" width="617" height="256"></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image025.jpg" alt="" width="617" height="308"></p>
<p><strong>This way of positioning the scales allows me to hide the zipper completely! </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image026.jpg" alt="" width="620" height="392"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image027.jpg" alt="" width="620" height="394"></strong></p>
<p><strong>The rest of the rows up to the top of the back will be made this way. I will replace 4.5cm scales with 4cm scales at the right time.</strong></p>
<p><strong>I could leave the scales layout for the front of the skirt unchanged but I believe it will look more beautiful with scales arranged symmetrically to the scales at the back of the skirt. </strong></p>
<p><strong>I mark symmetric guidelines on the front piece and start putting scales on it. </strong>First I fill in the central part and then follow the half-arc between the side seam edge and the centre line and the half-arc between the centre line and the other side seam edge.</p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image028.jpg" alt="" width="616" height="385"></p>
<p><img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image029.jpg" alt="" width="596" height="342"></p>
<p><strong>I have reached the top of the back and of the front with 4.5cm scales. Since the top edge of the front is V-shaped, I need to make three additional rows for proper alignment with the back. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image030.jpg" alt="" width="617" height="326"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2017.files/image032.jpg" alt="" width="617" height="310"></strong></p>
<p><strong>The remaining areas of the skirt will be covered by 4cm scales arranged the same way as the previous 4.5cm scales. </strong></p>
<p><strong>There will be a horizontal row of 4cm scales going along the stay-stitching at the top of the skirt which will overlay the corners formed by the overlapped scales put along the half-arc guidelines.</strong></p>
<p>Then I will press the decorated front and back on the table: first from the inside and then from the face (very carefully, without steam).</p>
<p><strong>After doing all this, I will only need to sew the side seams and decorate them with the scales which I had put aside in advance. </strong></p>

