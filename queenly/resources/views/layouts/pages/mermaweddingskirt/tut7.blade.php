<h1>Tutorial 6. Preparing Your Soldering Iron.</h1>
<p><strong>It is absolutely acceptable to use a plain household soldering iron when you don�t have a special pyrography tool. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%206.files/image002.jpg" alt=""></p>
<p><strong>Please make sure to pay attention to the following things when choosing a soldering iron for working with fabric:</strong></p>
<p><strong>- Handle and total length. You should feel comfortable with them. </strong>You want to be able to draw and cut any shapes on fabric.</p>
<p><strong>- Heating capacity. </strong>I would say that 25W is the best choice when you plan to work with fabric.</p>
<p><strong>- Well-sharpened soldering tip. </strong></p>
<p><strong>I would like to focus on what is a well-sharpened soldering tip. </strong>The quality of your garment depends on how well the soldering tip is sharpened. Using a poorly-sharpened soldering tip results in scorch marks and uneven edges.</p>
<p><strong>The easiest way is to sharpen a soldering tip with very fine grit sandpaper</strong> (see example below).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%206.files/image004.jpg" alt=""></p>
<p><strong>A well-sharpened soldering iron tip looks like this: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%206.files/image006.jpg" alt=""></p>
<p>The whole sharpening process literally takes just a couple of minutes. But you will have to repeat it every now and then because the tip naturally goes blunt in work.</p>
<p><strong>I hold the soldering tip at 45 degrees to the surface of the sandpaper when sharpening it. </strong>The whole process is illustrated in the pictures below.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%206.files/image007.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%206.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%206.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%206.files/image011.jpg" alt=""></p>
<p><strong>I have sharpened the tip of a new soldering iron the same way as on the soldering iron I work with. </strong></p>
