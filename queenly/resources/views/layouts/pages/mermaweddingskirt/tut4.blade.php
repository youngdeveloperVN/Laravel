<h1>Tutorial 3. Cutting the Skirt in a Fabric-Efficient Way.</h1>
<p><strong>The face of the skirt will be cut from satin</strong> (because I simply have some left from the previous orders). Of course, you could use cheaper fabric like crepe-back satin.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image002.jpg" alt="knub.rusal-024"></p>
<p><strong>The decorative elements, which we will discuss a little later, will be made of dense curtain fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image003.jpg" alt="knub.rusal-025"></p>
<p><strong>I will also need lining fabric, soft Rigilene boning tape, and a zipper. </strong></p>
<p><strong>I fold the satin in half. </strong>The skirt will be cut on the lengthwise grain.</p>
<p><strong>I put the pattern of the front to the fold line of the fabric and outline it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image005.jpg" alt="knub.rusal-026"></p>
<p><strong>I add a 1.2cm seam allowance along the top edge and prolong the centre front line by 10cm plus a 1.2cm seam allowance. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image007.jpg" alt="knub.rusal-027"></strong></p>
<p><strong>Then I prolong the side seam line by 15cm and add a 1.2cm seam allowance to it. </strong>I draw an arc-shaped bottom edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image009.jpg" alt="knub.rusal-028"></p>
<p><strong>Then I move to the back half of the skirt. </strong></p>
<p><strong>I start by cutting the two half-gores situated between the central back seam and the back princess seam. </strong></p>
<p><strong>I put the half-gore pattern to the selvage along the lengthwise grain, stepping 1cm from the edge to account for a zipper seam allowance. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image011.jpg" alt="knub.rusal-029"></p>
<p><strong>I outline the pattern and prolong the princess seam line by 28cm plus 1.2cm seam allowance. I also prolong the centre back line by 40cm and add a 1.2cm seam allowance to it. Remember to add a 1.2cm seam allowance along the top edge, too. Then I draw an arc-shaped bottom edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image013.jpg" alt="knub.rusal-030"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image015.jpg" alt="knub.rusal-031"></p>
<p><strong>Now I need to cut two full gores which will be situated between the side seams and the back princess seams. </strong></p>
<p><strong>First of all, I put the half-gore pattern along the lengthwise grain, outline it, prolong the side seam line by 15cm plus 1.2cm seam allowance, and add a 1.2cm seam allowance along the top edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image017.jpg" alt="knub.rusal-032"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image019.jpg" alt="knub.rusal-033"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image020.jpg" alt="knub.rusal-034"></p>
<p><strong>Then I remove 1cm at the top of the side seam in accordance with the calculations. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image021.jpg" alt="knub.rusal-036"></p>
<p><strong>I flip the pattern, align it with the marked vertical line, outline it, prolong the princess seam line by 28cm, and add a 1.2cm seam allowance to it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image023.jpg" alt="knub.rusal-035"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image025.jpg" alt="knub.rusal-037"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%203.files/image027.jpg" alt="knub.rusal-038"></p>
<p>I draw the bottom edge. Its shape will be adjusted later. <strong>I will polish up the shape of the train when the garment is finished. </strong></p>
<p><strong>It is a very fabric-efficient layout. All I need to do now is cut everything out. </strong></p>
<p><strong>I will cut the lining using the same pieces. Its length will be 100cm. </strong></p>

